                "ERROR 2: Starting an Euler trail at the wrong vertex. "
                "With exactly two odd vertices, every Euler trail runs "
                "from one odd vertex to the other. Starting at an even "
                "vertex guarantees you strand yourself. Identify the odd "
                "pair FIRST; they are your endpoints.\n\n"
                "ERROR 3: Off-by-one in edge counts. Trees: $n - 1$ "
