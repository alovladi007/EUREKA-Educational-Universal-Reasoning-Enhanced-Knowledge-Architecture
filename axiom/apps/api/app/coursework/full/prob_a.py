                "you, think it possible you may be mistaken' — and the practical "
                "rule is: never assign 0 or 1 to anything you might have to revise. "
                "Use $10^{-6}$, not 0. This is not pedantry; it is the mathematical "
                "form of dogmatism.\n\n"
