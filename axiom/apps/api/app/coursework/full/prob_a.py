                "You have now seen the same computation twice — once as a fraction of "
                "counts, once as a ratio of decimals. They are algebraically "
                "identical. They are NOT psychologically identical, and the "
                "difference is measurable.\n\n"
                "Gigerenzer and Hoffrage's studies are the famous evidence. Give "
                "physicians the mammography problem in probabilities ('prevalence "
                "1%, sensitivity 90%, false-positive rate 9% — what is $P(\\text{"
                "cancer} \\mid +)$?') and roughly 15% get it right; the modal answer "
                "is around 90%, off by a factor of ten. Restate the SAME problem in "
                "natural frequencies ('10 of every 1,000 women have cancer; 9 of "
                "those 10 test positive; 89 of the other 990 also test positive') and "
                "the success rate jumps to about half — with no training. The "
                "arithmetic didn't get easier. The BOOKKEEPING got visible: the phrase "
                "'89 of the other 990' makes the false-positive branch impossible to "
                "delete, and deleting it is the error.\n\n"
                "THE METHOD.\n"
                "1. Name the hypothesis partition. Mutually exclusive? Exhaustive? "
                "(Page 13's silent killer lives here.)\n"
                "2. Choose $N$ so every count is a whole number — usually the "
                "reciprocal of the smallest probability in the problem. Prevalence "
                "$1\\%$ with $5\\%$ error rates → $N = 10{,}000$. Prevalence "
                "$1$-in-$10^6$ → $N = 10^8$.\n"
                "3. Split $N$ by prior. Do the pieces re-add to $N$?\n"
                "4. Within each piece, apply the likelihood to get the evidence-"
                "matching count.\n"
                "5. ADD the matching counts across ALL branches. That total is the "
                "denominator.\n"
                "6. Divide the branch you want by that total.\n"
                "7. Sanity-check: is the posterior on the correct side of the prior? "
                "$\\mathrm{LR} > 1$ must move it UP, $\\mathrm{LR} < 1$ DOWN, and it "
                "must stay in $[0,1]$.\n\n"
