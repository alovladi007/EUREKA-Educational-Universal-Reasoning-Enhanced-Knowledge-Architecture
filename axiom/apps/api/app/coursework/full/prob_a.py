                "• $P(H)$ — the PRIOR. What you believed before the evidence. Often "
                "an objective base rate: disease prevalence, the fraction of parts "
                "from machine $M_1$.\n"
                "• $P(E \\mid H)$ — the LIKELIHOOD of the evidence under $H$. NOT a "
                "probability distribution over $H$: as $H$ ranges over hypotheses, "
                "the likelihoods need NOT sum to 1, and usually don't.\n"
                "• $P(E)$ — the EVIDENCE, or marginal likelihood. The normalizing "
                "constant: how likely the data were, averaged over all "
                "hypotheses.\n"
                "• $P(H \\mid E)$ — the POSTERIOR. What you believe now.\n\n"
