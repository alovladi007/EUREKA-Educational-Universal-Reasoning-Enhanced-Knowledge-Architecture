                "$S$: $P(A) + P(A^c) = P(S) = 1$, so "
                "$P(A^c) = 1 - P(A)$. PS01's 'at least one' trick is now a "
                "theorem for ALL models, not just uniform ones.\n\n"
                "RULE 3 (MONOTONICITY). If $A \\subseteq B$, split $B$ into "
                "disjoint pieces $A$ and $B \\setminus A$: "
                "$P(B) = P(A) + P(B \\setminus A) \\ge P(A)$ by Axiom 1. "
