                "The formula $P(A) = \\frac{|A|}{|S|}$ has ONE hypothesis: the "
                "outcomes you are counting must be equally likely. Violate it "
                "and the fraction computes nothing. Three ways it fails:\n\n"
                "FAILURE 1: COUNTING SUMMARIES INSTEAD OF OUTCOMES. Two dice "
                "produce sums $2$ through $12$ — eleven possibilities — but "
                "they are NOT equally likely: only one ordered pair gives 12, "
                "while six give 7. The equally-likely objects are the 36 ordered "
                "pairs; the sums are summaries that bundle unequal numbers of "
                "them. Any derived quantity (sums, counts, maxima) must be "
                "traced back to the raw uniform space.\n\n"
                "FAILURE 2: THE 'TWO OUTCOMES, SO 50/50' FALLACY. 'Either I win "
                "the lottery or I don't — two outcomes, so it's 50/50.' "
                "Enumerating possibilities does not make them equally likely; "
                "only symmetry does, and there is no symmetry between one "
                "ticket and 13.9 million. Whenever you hear a probability "
                "claimed, ask: WHAT symmetry makes these outcomes "
                "interchangeable? No symmetry, no uniform formula.\n\n"
                "FAILURE 3: BROKEN PHYSICAL SYMMETRY. A loaded die, a bent "
                "coin, a thumbtack (point-up versus point-down has no symmetry "
