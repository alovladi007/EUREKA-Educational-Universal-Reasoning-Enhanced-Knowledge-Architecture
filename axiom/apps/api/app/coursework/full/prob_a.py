                "Its power is entirely in what you must supply to use it.\n\n"
                "THE DENOMINATOR IS THE WORK. $P(E)$ is almost never handed to you. "
                "You EXPAND it by total probability over the hypothesis partition. "
                "For a two-hypothesis split ($H$ and its complement $H^c$):\n"
                "$$P(H \\mid E) = \\frac{P(E \\mid H)P(H)}{P(E \\mid H)P(H) + "
