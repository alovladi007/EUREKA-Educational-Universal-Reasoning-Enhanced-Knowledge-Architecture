                "ZERO VARIANCE. $\\operatorname{Var}(X) = 0$ forces every "
                "squared deviation's probability-weighted sum to vanish, "
                "which forces $X = \\mu$ with probability 1. Constants are "
                "exactly the spread-free variables; any claimed distribution "
                "with two attainable values and zero variance is "
