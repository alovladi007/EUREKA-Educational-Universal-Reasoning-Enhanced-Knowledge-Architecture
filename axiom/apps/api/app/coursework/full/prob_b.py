                "second-moment series $\\sum k^{-1}$ diverges (C210's "
                "$p$-series). Heavy-tailed models of incomes, insurance "
                "losses, and market crashes live in this regime — the mean "
                "exists, but 'typical deviation' does not, and "
                "average-based reasoning gets treacherous.\n\n"
                "CHEBYSHEV'S INEQUALITY. Whenever $\\sigma$ IS finite, it "
                "controls tails universally:\n"
                "$$P(|X - \\mu| \\ge k\\sigma) \\le \\frac{1}{k^2}.$$\n"
                "No assumptions about the distribution's shape — at most a "
                "quarter of the probability lies 2$\\sigma$ or more from the "
                "mean, at most a ninth beyond 3$\\sigma$. Crude but "
                "unconditional; PS10's normal distribution sharpens these "
                "to 5% and 0.3% under stronger hypotheses.\n\n"
                "THE AVERAGE OF $n$ COPIES — the chapter's most consequential "
                "special case. For independent $X_1, \\ldots, X_n$ with "
                "common mean $\\mu$ and variance $\\sigma^2$, the sample "
                "mean $\\bar X = \\frac{1}{n}(X_1 + \\cdots + X_n)$ "
