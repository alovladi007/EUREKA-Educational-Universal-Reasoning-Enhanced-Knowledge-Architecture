                "$\\lambda \\to 0$. Mass piles onto zero: $P(0) = e^{-\\lambda} "
                "\\approx 1 - \\lambda$, $P(1) \\approx \\lambda$, and $P(\\ge 2) "
                "\\approx \\frac{\\lambda^2}{2}$ — negligible. A tiny-$\\lambda$ "
                "Poisson is a Bernoulli in disguise, and the useful approximation "
                "$P(\\ge 1) \\approx \\lambda$ falls right out.\n\n"
                "$\\lambda \\to \\infty$. The skewness is $\\frac{1}{\\sqrt\\lambda} "
                "\\to 0$: the distribution straightens into a bell. For $\\lambda "
                "\\gtrsim 10$, $X \\approx N(\\lambda, \\lambda)$ with a continuity "
                "correction (PS10). This is no accident — $\\text{Pois}(\\lambda)$ is "
                "the sum of $\\lambda$ independent $\\text{Pois}(1)$'s (superposition, "
                "page 6), so the central limit theorem (PS11) applies directly.\n\n"
                "$k \\to \\infty$. The $k!$ in the denominator beats every "
                "exponential, so Poisson tails are THIN — thinner than exponential. "
                "$\\text{Pois}(3)$ at $k = 15$ gives $5.5 \\times 10^{-7}$. "
                "Consequence: if your real data throw occasional enormous counts, "
                "Poisson will not merely under-predict them, it will call them "
                "impossible. Heavy-tailed count data needs a different model.\n\n"
