                "complement. Memorize $P(X \\ge 1) = 1 - e^{-\\lambda}$; it answers "
                "perhaps a third of all Poisson questions asked in practice.\n\n"
                "STEP 5 — COMPUTE with the recursion $P(k) = \\frac{\\lambda}{k}"
                "P(k-1)$ whenever you need more than one term. It avoids $\\lambda^k$ "
                "and $k!$ entirely — no overflow, no lost precision.\n\n"
                "STEP 6 — SANITY-CHECK. Mean $\\lambda$, $\\sigma = "
                "\\sqrt{\\lambda}$. Is your answer in $[0,1]$? Does the bulk sit "
                "within a couple of $\\sqrt{\\lambda}$ of $\\lambda$?\n\n"
                "THE DISPERSION CHECK — the technique that makes Poisson honest. "
                "Because mean $=$ variance is a CONSEQUENCE, it is a testable "
