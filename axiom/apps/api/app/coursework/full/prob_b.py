                "volume at lunchtime, ER arrivals on Saturday night. Fix: a "
                "nonhomogeneous process with $\\lambda(t)$, integrated over the "
                "window.\n"
                "2. INDEPENDENCE fails under contagion or clustering: one accident "
                "causes the next, one infection causes ten. Symptom: OVERDISPERSION, "
                "$s^2 \\gg \\bar x$.\n"
                "3. NO SIMULTANEITY fails with batch arrivals: a bus disgorges 40 "
                "customers at once. Fix: compound Poisson (Poisson arrivals of "
                "random-sized batches).\n"
                "4. A FIXED $\\lambda$ fails when the rate itself is random across "
