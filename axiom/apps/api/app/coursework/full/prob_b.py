                "• Is the rate CONSTANT across the window? (Lunchtime call surges "
                "break this.)\n"
                "• Are events INDEPENDENT? (Multi-car pileups, contagion, and "
                "retweets break this.)\n"
                "• Can events coincide? (Batch arrivals break this.)\n"
                "All five, or reach for page 10's alternatives.\n\n"
                "STEP 2 — RESCALE. $\\lambda_{\\text{window}} = \\text{rate} \\times "
                "\\text{window}$, with units cancelling. $3/\\text{hr}$ over 20 min "
                "→ $\\lambda = 1$. $0.2/\\text{page}$ over 25 pages → $\\lambda = 5$. "
                "$0.6/\\text{m}^2$ over a $3 \\times 2.5$ panel → $\\lambda = 4.5$. "
                "The window can be anything measurable; only the product matters.\n\n"
                "STEP 3 — TRANSLATE THE WORDS into an explicit $k$-set. 'At least 3' "
                "$= \\{3, 4, 5, \\ldots\\}$; 'fewer than 3' $= \\{0, 1, 2\\}$; 'more "
