                "STEP 2 — the obstruction. A distance-preserving map (isometry) must, by "
                "the Theorema Egregium, preserve Gaussian curvature pointwise. Preserving "
                "$K$ would require $\\frac{1}{R^2} = 0$, which is impossible for finite "
                "$R$. Hence NO isometry from sphere to plane exists: distances MUST "
                "distort. This is not an engineering limitation but a theorem.\n\n"
                "STEP 3 — quantify with a geodesic triangle. Consider a triangle on the "
                "sphere bounded by geodesics (great-circle arcs): run from the north pole "
                "down to the equator, along the equator through $90^\\circ$ of longitude, "
                "and back up to the pole. Each of its three angles is $90^\\circ$, so the "
                "angle sum is $270^\\circ$ — an EXCESS of $90^\\circ = \\frac{\\pi}{2}$ "
                "over the flat-plane value of $180^\\circ$.\n\n"
                "STEP 4 — connect to curvature via Gauss-Bonnet. The angle excess of a "
                "geodesic triangle equals the integral of curvature over its interior: "
                "$\\text{excess} = \\iint_{\\triangle} K\\,dA = \\frac{1}{R^2} \\cdot "
                "\\text{Area}$. Our triangle covers one-eighth of the sphere, area "
                "$\\frac{1}{8}(4\\pi R^2) = \\frac{\\pi R^2}{2}$, giving excess "
                "$\\frac{1}{R^2}\\cdot\\frac{\\pi R^2}{2} = \\frac{\\pi}{2}$ — exactly "
                "the $90^\\circ$ we counted. The two computations agree.\n\n"
                "CONCLUSION. Any flat map, having zero curvature, cannot reproduce this "
                "$90^\\circ$ angle excess without stretching some distances. The larger "
                "the region mapped, the greater the accumulated distortion — which is "
                "precisely why local street maps are nearly perfect but world maps are "
