                "2\\pi\\chi$. Dropping the boundary terms gives wrong answers for "
                "triangles and disks.\n\n"
                "ERROR 6: Treating a coordinate singularity as a geometric one — "
                "declaring the sphere 'singular at the poles' when only the "
                "parametrization degenerated there. Check with a different chart before "
                "claiming a real singularity."
