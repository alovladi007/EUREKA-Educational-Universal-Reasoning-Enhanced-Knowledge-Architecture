                "ERROR 6: Treating $\\sup$ as attained inside integrals or extremal "
                "arguments. Always ask whether the supremum is a maximum; if it is not "
                "attained, an argument that 'picks the point where the max occurs' is "
                "invalid on an open or unbounded domain."
