                "MISCONCEPTION 2: 'Wave solutions have to be sinusoidal.' No — ANY twice-"
                "differentiable profile travels. Sinusoids are popular because Fourier "
                "analysis builds everything from them, but a triangular pluck or a "
                "Gaussian bump is an honest solution. The wave equation is "
                "shape-preserving for ALL shapes.\n\n"
