                "DISPERSIVE LINEAR media are pure sinusoids the shape-preserving waves. "
                "The wave equation carries any profile; solitons are $\\operatorname{"
                "sech}^2$ pulses; shocks are $\\tanh$ fronts. The sinusoid is special, "
                "not universal.\n\n"
                "MISCONCEPTION 2: 'Any speed $c$ gives a valid travelling wave.' For the "
                "wave equation the ansatz forces $c = \\pm a$; for solitons the speed is "
                "tied to amplitude; for Fisher fronts $c \\ge 2\\sqrt{Dr}$. The profile "
                "ODE plus boundedness usually CONSTRAINS $c$ severely. Never assume $c$ "
                "is free.\n\n"
                "MISCONCEPTION 3: 'Phase velocity is the speed of the signal/energy.' In "
                "a dispersive medium the ENERGY and the packet envelope move at the "
                "GROUP velocity $d\\omega/dk$, which differs from the phase velocity "
                "$\\omega/k$. Confusing them mispredicts when a pulse actually "
                "arrives.\n\n"
                "MISCONCEPTION 4: 'Dispersion means the wave loses energy.' No — "
                "dispersion (conservative) SPREADS a packet by separating wavelengths, "
                "while energy is conserved. Loss of energy is DISSIPATION (e.g. "
                "viscosity), a different effect. Burgers has dissipation; linear KdV has "
                "dispersion; they are not the same.\n\n"
                "MISCONCEPTION 5: 'A soliton is just a bump that happens to travel.' Its "
                "defining magic is that NONLINEARITY (steepening) and DISPERSION "
                "(spreading) exactly BALANCE, so it keeps its shape and even survives "
                "collisions with other solitons. Remove either ingredient and the "
                "balance — and the soliton — is gone.\n\n"
                "MISCONCEPTION 6: 'The diffusion equation supports travelling waves.' "
                "Pure diffusion does not (page 4): its only bounded profile is constant. "
                "Fronts appear only when diffusion is paired with REACTION (Fisher) or "
                "NONLINEAR ADVECTION (Burgers)."
