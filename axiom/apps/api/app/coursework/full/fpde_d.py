                "Linearize at the LEADING EDGE $\\phi \\approx 0$: $D\\phi'' + c\\phi' + "
                "r\\phi = 0$, with roots $\\lambda = \\frac{-c \\pm \\sqrt{c^2 - "
                "4Dr}}{2D}$. Here is the selection mechanism. A population density "
                "cannot go NEGATIVE; if the roots are complex ($c^2 < 4Dr$), $\\phi$ "
                "oscillates through zero into unphysical negative values. To keep "
                "$\\phi \\ge 0$ the roots must be REAL, forcing\n"
                "$$c \\ge c^{*} = 2\\sqrt{Dr}.$$\n"
