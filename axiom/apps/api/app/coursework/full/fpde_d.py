            (L, "Dispersion relations for linear equations", (
                "For LINEAR PDEs there is a sharper tool than the general profile ODE: "
                "test sinusoidal travelling waves. Substitute the complex plane wave\n"
                "$$u(x,t) = e^{i(kx - \\omega t)}$$\n"
                "(wavenumber $k$, angular frequency $\\omega$) into the equation. Because "
                "$\\partial_t \\to -i\\omega$ and $\\partial_x \\to ik$, the PDE becomes "
                "an ALGEBRAIC relation between $\\omega$ and $k$, the DISPERSION "
                "RELATION $\\omega = \\omega(k)$.\n\n"
                "The plane wave is a travelling wave of speed $c = \\omega/k$, the PHASE "
                "SPEED. The crucial question is whether this speed depends on $k$:\n"
                "- If $\\omega/k$ is CONSTANT (independent of $k$), all wavelengths "
                "travel at the same speed. The medium is NON-DISPERSIVE; a wave packet "
                "holds together. The wave equation $u_{tt} = c^2 u_{xx}$ gives "
                "$\\omega^2 = c^2 k^2$, so $\\omega/k = \\pm c$ — non-dispersive, "
                "consistent with 'any shape travels.'\n"
                "- If $\\omega/k$ DEPENDS on $k$, different wavelengths travel at "
                "different speeds. The medium is DISPERSIVE; an initially sharp packet "
                "SPREADS as its component sinusoids separate. This is why a general "
                "profile CANNOT survive intact in a dispersive medium — only pure "
                "sinusoids keep their shape.\n\n"
                "A second speed matters for packets: the GROUP VELOCITY $v_g = "
                "d\\omega/dk$, the speed at which energy and the packet ENVELOPE travel, "
                "generally different from the phase speed $\\omega/k$ when there is "
                "dispersion. For deep-water waves, for instance, the group velocity is "
                "half the phase velocity — crests appear to run through the packet and "
                "vanish at its front.\n\n"
                "So dispersion relations answer 'which sinusoids are allowed and how "
                "fast do they go,' and their $k$-dependence is the precise dividing line "
                "between media that transmit signals faithfully (like the ideal string) "
                "and media that smear them (like a stiff beam or a plasma)."
            )),
