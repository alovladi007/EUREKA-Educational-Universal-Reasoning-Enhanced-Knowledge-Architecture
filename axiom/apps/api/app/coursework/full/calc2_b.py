            (L, "The p-integrals and the comparison test", (
                "The workhorse family is $f(x) = \\frac{1}{x^p}$, and its "
                "behavior is worth memorizing cold.\n\n"
                "AT INFINITY: $\\displaystyle\\int_1^\\infty \\frac{dx}{x^p}$ "
                "converges if and only if $p > 1$, with value "
                "$\\frac{1}{p-1}$. Proof sketch: for $p \\ne 1$ the "
                "antiderivative is $\\frac{x^{1-p}}{1-p}$; as "
                "$x \\to \\infty$ this tends to 0 precisely when the "
                "exponent $1 - p$ is negative, i.e. $p > 1$. The boundary "
                "case $p = 1$ gives $\\ln t \\to \\infty$: diverges.\n\n"
                "AT ZERO: $\\displaystyle\\int_0^1 \\frac{dx}{x^p}$ converges "
                "if and only if $p < 1$, with value $\\frac{1}{1-p}$. Same "
                "computation, opposite inequality.\n\n"
                "Notice the beautiful duality: at $\\infty$ you need FAST "
                "decay ($p > 1$); at $0$ you need a MILD blow-up "
                "($p < 1$); and $p = 1$ — the curve $\\frac1x$ — fails at "
                "both ends. Exponentials, for contrast, always dominate: "
                "$\\int_0^\\infty e^{-kx}dx = \\frac1k$ for any $k > 0$.\n\n"
                "THE COMPARISON TEST. Suppose $0 \\le f(x) \\le g(x)$ on the "
                "interval of integration. Then:\n"
                "• if $\\int g$ converges, so does $\\int f$ (smaller than "
                "finite is finite);\n"
                "• if $\\int f$ diverges, so does $\\int g$ (bigger than "
                "infinite is infinite).\n\n"
                "Mind the directions — a function SMALLER than a divergent "
                "one, or BIGGER than a convergent one, tells you nothing. "
                "The test converts 'is this integral finite?' into 'can I "
                "trap the integrand against a $p$-integral or an "
                "exponential?' — which is usually a one-line inequality. It "
                "decides convergence WITHOUT computing the value, which is "
                "exactly what you need for integrands like $e^{-x^2}$ that "
                "have no elementary antiderivative at all."
            )),
