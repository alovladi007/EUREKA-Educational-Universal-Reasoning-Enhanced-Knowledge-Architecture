            (L, "Main properties and theorems", (
                "THEOREM 1 (closure under linear maps). If "
                "$X \\sim N(\\mu, \\sigma^2)$ and $a \\ne 0$, then "
                "$aX + b \\sim N(a\\mu + b,\\; a^2\\sigma^2)$. Rescaling and "
                "shifting a normal yields a normal — change celsius to "
                "fahrenheit, meters to feet, and the bell survives with "
                "transformed parameters. Standardization is the special case "
                "$a = \\frac{1}{\\sigma}$, $b = -\\frac{\\mu}{\\sigma}$: it "
                "is a theorem, not a definition, that $Z$ is standard "
                "normal.\n\n"
                "THEOREM 2 (closure under sums). If $X \\sim N(\\mu_X, "
                "\\sigma_X^2)$ and $Y \\sim N(\\mu_Y, \\sigma_Y^2)$ are "
                "INDEPENDENT, then\n"
                "$$X + Y \\sim N(\\mu_X + \\mu_Y,\\; \\sigma_X^2 + "
                "\\sigma_Y^2).$$\n"
                "Means add; VARIANCES add (never standard deviations — the SD "
                "of the sum is $\\sqrt{\\sigma_X^2 + \\sigma_Y^2}$, the "
                "Pythagorean combination from PS06). Sums of independent "
                "normals are exactly normal, no approximation — a property so "
                "special it essentially characterizes the family, and the "
                "seed of the central limit theorem.\n\n"
                "THEOREM 3 (the empirical rule, made exact). "
                "$P(|X - \\mu| \\le \\sigma) = 2\\Phi(1) - 1 = 0.6827$; "
                "within $2\\sigma$: $2\\Phi(2) - 1 = 0.9545$; within "
                "$3\\sigma$: $2\\Phi(3) - 1 = 0.9973$. The 68-95-99.7 slogan "
                "is these three numbers rounded.\n\n"
                "PERCENTILES run the table backwards. The $p$-th percentile of "
                "$N(\\mu, \\sigma^2)$ is\n"
                "$$x_p = \\mu + z_p\\,\\sigma, \\qquad \\text{where } "
                "\\Phi(z_p) = p.$$\n"
                "Three inverse values recur so often they deserve memory: "
                "$z_{0.90} = 1.2816$, $z_{0.95} = 1.6449$, "
                "$z_{0.975} = 1.9600$. That last one — 1.96 SDs capturing the "
                "middle 95% — will follow you through PS13, PS14, and every "
                "confidence interval you ever build."
            )),
