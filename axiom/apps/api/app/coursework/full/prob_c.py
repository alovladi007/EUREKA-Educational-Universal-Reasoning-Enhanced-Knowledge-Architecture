                "which table is right? Both are correct arithmetic, but 'is there "
                "bias?' is a CAUSAL question no description answers. If department "
                "is a CONFOUNDER — affecting both where you apply and your odds — "
                "condition on it and the departmental table is honest. But if "
                "department choice is a MEDIATOR — say, departments serving women "
                "were underfunded and so rejected more — then conditioning on it "
                "HIDES the discrimination and the aggregate is honest. The data is "
                "identical in both worlds; Judea Pearl's causal framework exists "
                "to make this choice explicit rather than accidental.\n\n"
