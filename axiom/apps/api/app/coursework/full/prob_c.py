                "$75/300 = 25\\%$.\n\n"
                "Women are admitted at a HIGHER rate in EVERY department "
                "($65 > 60$, $25 > 20$). Now total: men $\\frac{180+20}{400} = "
                "50\\%$; women $\\frac{65+75}{400} = 35\\%$. Women win every "
                "department and lose the aggregate by fifteen points. Nothing was "
                "miscounted; check the sums.\n\n"
                "THE MECHANISM. An aggregate rate is a WEIGHTED average of "
                "subgroup rates, weighted by where each group applied:\n"
                "$$\\text{men} = 0.75(0.60) + 0.25(0.20) = 0.50, \\qquad "
                "\\text{women} = 0.25(0.65) + 0.75(0.25) = 0.35.$$\n"
                "The RATES favor women; the WEIGHTS bury them — men mostly applied "
                "to the easy department, women to the hard one. Page 13's Error 3 "
                "with the weighting done correctly, and the reversal happens "
                "anyway.\n\n"
                "Not hypothetical: Bickel, Hammel and O'Connell (Science, 1975) "
                "analysed Berkeley's 1973 graduate admissions, where the aggregate "
                "showed roughly $44\\%$ of men and $35\\%$ of women admitted, yet "
                "department by department the picture was neutral or slightly "
                "favoured women — who had applied disproportionately to "
                "low-admission departments.\n\n"
