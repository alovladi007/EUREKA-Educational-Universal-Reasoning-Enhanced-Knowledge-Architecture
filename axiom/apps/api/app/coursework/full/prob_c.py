                "Every subgroup can go one way while the total goes the other — "
                "not a trick, an error, or a small-sample artifact, but a "
                "permanent feature of averaging, and the reason descriptive "
                "statistics alone cannot answer causal questions.\n\n"
                "A clean instance. Two graduate departments; applicants and "
                "admits by gender.\n\n"
                "DEPARTMENT A (admits generously): men $180/300 = 60\\%$; women "
