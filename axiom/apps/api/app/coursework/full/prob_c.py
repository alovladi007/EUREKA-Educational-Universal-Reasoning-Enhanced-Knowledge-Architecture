                "ERROR 1: WRONG TAIL. The table gives $\\Phi(z) = "
                "P(Z \\le z)$; questions constantly ask for $P(X > x)$. "
                "Forgetting the $1 - {}$ flips 0.05 into 0.95 — a "
                "catastrophic, common, and entirely sketch-preventable "
                "error. Shade first; if the shade covers a minority of the "
                "bell, the answer must be under 0.5.\n\n"
                "ERROR 2: STANDARDIZING WITH THE VARIANCE. "
                "$z = \\frac{x - \\mu}{\\sigma}$, never $\\frac{x - \\mu}"
                "{\\sigma^2}$. Given '$N(50, 16)$', the divisor is 4. "
                "Symptom: z-scores like 0.06 or 40 — absurdly small or "
                "large. Reasonable data gives $|z| \\lesssim 4$.\n\n"
                "ERROR 3: SIGN LOSS ON NEGATIVE $z$. $P(X < $ "
                "below-mean cutoff$)$ must come out under 0.5. Tables "
                "listing only positive $z$ force the conversion "
                "$\\Phi(-z) = 1 - \\Phi(z)$; dropping it reports 0.8944 "
                "where 0.1056 belongs. The sketch catches this too.\n\n"
                "ERROR 4: BACKWARD-PROBLEM CONFUSION. Looking up 0.90 in "
                "the BODY of the table (finding the z whose area is 0.90: "
                "1.2816) is correct for percentile problems; looking up "
                "$z = 0.90$ and reporting $\\Phi(0.90) = 0.8159$ is the "
                "classic direction error. Backward problems read the table "
                "inside-out. Label the problem F or B before starting.\n\n"
                "ERROR 5: ADDING STANDARD DEVIATIONS. For independent sums, "
                "VARIANCES add: SDs combine as "
                "$\\sqrt{\\sigma_1^2 + \\sigma_2^2}$. Three $\\sigma = 0.1$ "
