                "flips: binomial histograms approach "
                "$e^{-x^2/2}$. Laplace (1810) grasped the generality — "
                "sums of many independent errors, whatever their law — "
                "and used it throughout celestial mechanics. Full "
                "rigor waited a century: Lyapunov (1901) gave the "
                "first modern proof, and Lindeberg (1922) found "
                "essentially the weakest hypotheses under which it "
                "holds. The name 'central limit theorem' is Pólya's "
                "(1920): the limit theorem CENTRAL to probability.\n\n"
                "THE PROOF IDEA, in a paragraph. Adding independent "
                "random variables CONVOLVES their densities — messy. "
                "But the Fourier transform (PF.U2.N2) converts "
                "convolution into multiplication: the characteristic "
                "function $\\varphi(t) = E[e^{itX}]$ of a "
                "standardized sum is a PRODUCT of $n$ individual "
                "factors. Taylor-expand one factor: "
                "$\\varphi(t/\\sqrt{n}) \\approx 1 - \\frac{t^2}{2n}$ "
                "— the mean-zero, variance-one structure appears in "
                "the first two terms, and EVERYTHING ELSE about the "
                "population hides in higher-order terms that vanish. "
                "Then $\\left(1 - \\frac{t^2}{2n}\\right)^n \\to "
                "e^{-t^2/2}$ — the transform of the standard normal "
                "(the Gaussian's fixed-point property from PS10). "
                "Universality demystified: averaging simply forgets "
                "every population detail except mean and variance.\n\n"
                "REFINEMENTS worth knowing exist. The BERRY-ESSEEN "
                "theorem bounds the approximation error explicitly — "
                "error $\\le \\frac{0.47\\,E|X-\\mu|^3}{\\sigma^3 "
