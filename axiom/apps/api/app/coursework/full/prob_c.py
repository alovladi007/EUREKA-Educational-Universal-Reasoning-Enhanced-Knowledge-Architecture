                "AGAINST the normal model rather than cosmic bad luck. "
                "Real-world tails are often HEAVIER than normal: rare events "
                "occur more often than the bell predicts. Using normal tails "
                "where they don't belong is the most expensive modeling "
                "error in finance and risk management.\n\n"
                "WHEN TO DOUBT NORMALITY, on sight:\n"
                "• SKEWED data — incomes, house prices, waiting times, "
                "anything bounded below at 0 with a long upper tail.\n"
                "• BOUNDED data near its bound — percentages piling up near "
