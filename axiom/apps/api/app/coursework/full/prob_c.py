                "Every normal problem yields to the same five-step drill.\n\n"
                "STEP 1: SKETCH AND SHADE. Draw the bell, mark $\\mu$, mark "
                "the cutoff(s), shade the region the question asks about. Ten "
                "seconds, and it converts word-problem fog ('at most', "
                "'exceeds', 'within') into a picture whose area you can "
                "eyeball. Every later arithmetic result gets checked against "
                "this sketch.\n\n"
                "STEP 2: CLASSIFY THE DIRECTION. Forward (given cutoff, want "
                "probability) or backward (given probability/percentile, want "
                "cutoff)? Forward: standardize, then $\\Phi$. Backward: "
                "$\\Phi^{-1}$, then de-standardize. Never mix the orders.\n\n"
                "STEP 3: STANDARDIZE — $z = \\frac{x - \\mu}{\\sigma}$ — and "
                "carry three decimals in $z$; rounding $z$ to one decimal can "
                "shift the final probability by a full percent.\n\n"
                "STEP 4: EXPRESS VIA $\\Phi$ OF SOMETHING. Three canonical "
                "forms cover everything:\n"
                "$$P(X \\le a) = \\Phi(z_a), \\quad "
                "P(X > a) = 1 - \\Phi(z_a), \\quad "
                "P(a < X < b) = \\Phi(z_b) - \\Phi(z_a).$$\n"
                "If your table only lists positive $z$, convert with "
                "$\\Phi(-z) = 1 - \\Phi(z)$.\n\n"
                "STEP 5: INTERPRET IN CONTEXT with units and a plain-language "
                "sentence — '24% of students score below 65' — and confirm "
                "the number matches the shaded sketch (shaded most of the "
                "curve but got 0.24? A direction error, caught).\n\n"
                "COMPARING ACROSS SCALES: when two quantities live on "
                "different scales (different tests, species, markets), "
                "compare Z-SCORES, not raw values or raw gaps. The z-score is "
                "the universal currency of 'how unusual': it divides distance "
                "from the mean by the scale's own yardstick. This works as a "
                "descriptive comparison for any roughly-symmetric data; "
                "converting a z-score into a PROBABILITY, though, requires "
                "actual normality — the distinction page 12 sharpens."
