                "\\infty)$). These are theorems, not observations — each traces back "
                "to a limit computation, e.g. polynomial continuity IS C102's direct "
                "substitution theorem.\n\n"
                "THE ALGEBRA OF CONTINUITY. If $f$ and $g$ are continuous at $a$, so "
                "are $f + g$, $f - g$, $cf$, and $fg$; so is $\\frac{f}{g}$ provided "
                "$g(a) \\ne 0$; and compositions behave perfectly: if $g$ is "
                "continuous at $a$ and $f$ is continuous at $g(a)$, then $f \\circ g$ "
                "is continuous at $a$. Each clause is the corresponding C102 limit "
                "law, restated.\n\n"
                "THE PRACTICAL CONSEQUENCE. Any function ASSEMBLED from catalog "
                "members by arithmetic and composition is continuous everywhere it is "
                "defined. So $\\frac{e^{\\sin x}}{x^2 + 1}$ is continuous on all of "
                "$\\mathbb{R}$ — no audit needed. Discontinuities can hide only in "
                "specific places: where a denominator vanishes, where a piecewise "
                "definition changes formula, at domain edges of roots and logs, and "
                "in deliberately exotic constructions (page 10).\n\n"
                "Also unlocked: the SUBSTITUTION THEOREM for limits, "
                "$\\lim_{x \\to a} f(g(x)) = f(\\lim_{x \\to a} g(x))$ whenever $f$ "
                "is continuous at the inner limit — the formal version of C102's "
