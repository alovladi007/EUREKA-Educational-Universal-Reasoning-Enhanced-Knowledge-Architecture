                "Auditing every point from scratch would be unbearable. Two theorems "
                "make continuity checkable at a glance for almost every function you "
                "will ever write down.\n\n"
                "THE CATALOG. The following are continuous at every point of their "
                "domains: polynomials (domain: all reals); rational functions "
                "(wherever the denominator is nonzero); root functions; $|x|$; "
                "$\\sin x$ and $\\cos x$ (all reals); $\\tan x$ (except odd multiples "
