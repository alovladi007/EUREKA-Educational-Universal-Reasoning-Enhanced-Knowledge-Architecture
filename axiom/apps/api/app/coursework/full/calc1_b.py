                "Three recurring job types, each with a procedure.\n\n"
                "JOB 1: AUDIT A POINT. Run the three checks in order: value defined? "
                "limit exists? equal? Stop at the first failure and report it. For "
                "piecewise functions, the limit check usually splits into one-sided "
                "limits computed from DIFFERENT pieces; the value comes from whichever "
                "piece owns the boundary (the one with $\\le$ or $\\ge$).\n\n"
                "JOB 2: CLASSIFY A FAILURE. Use the limit's behavior:\n\n"
                "• Limit exists (value missing or mismatched) → REMOVABLE: redefining "
                "one point repairs it.\n"
                "• Both one-sided limits exist but differ → JUMP: no repair; the gap "
                "$|\\text{right} - \\text{left}|$ is the jump size.\n"
                "• At least one side is unbounded → INFINITE: vertical asymptote "
                "territory (C104 completes this story).\n"
                "• No one-sided limit exists even as $\\pm\\infty$ (oscillation) → "
                "ESSENTIAL: the pathological bin, e.g. $\\sin\\frac1x$ at 0.\n\n"
                "JOB 3: DESIGN FOR CONTINUITY. Given a piecewise function with unknown "
                "constants, write down the matching equation at EACH seam: (left "
                "piece's limit) $=$ (right piece's limit) $=$ (owner piece's value at "
                "the seam). With the value supplied by one of the pieces, this is "
                "simply: set the two one-sided limits equal. One seam and one unknown "
                "→ one equation. Two seams and two unknowns → a $2 \\times 2$ linear "
                "system (page 9). Solve, then VERIFY by substituting back into both "
                "pieces.\n\n"
                "WHERE TO HUNT for discontinuities when asked 'find all': vanishing "
                "denominators (after cancelling!), piecewise seams, domain edges of "
                "$\\sqrt{\\;}$ and $\\ln$, and floor/step constructions at integers. "
                "Everything else — catalog functions and their combinations — is "
