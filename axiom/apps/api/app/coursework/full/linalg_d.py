                "determinant $10$, both identities confirm you ($2 + 5 = 7$, "
                "$2 \\cdot 5 = 10$). If they fail, you made an arithmetic error. For a "
                "$2 \\times 2$ they can even hand you the eigenvalues directly: two "
                "numbers with a known sum and product are the roots of "
                "$\\lambda^2 - (\\text{sum})\\lambda + (\\text{product})$."
