                "antiderivative case (ODE.U0.N1).\n\n"
                "STEP 3 — Does the right side FACTOR as $g(x)\\,h(y)$? If yes, it is "
                "SEPARABLE: divide by $h(y)$, multiply by $dx$, integrate both sides "
                "(ODE.U1.N1). Cheapest method — always test for it first.\n\n"
                "STEP 4 — Can you write it as $y' + p(x)y = q(x)$? Then it is LINEAR: "
                "use integrating factor $\\mu = e^{\\int p\\,dx}$ (ODE.U1.N2). Note some "
                "equations are BOTH separable and linear; either route works.\n\n"
                "STEP 5 — Write it as $M\\,dx + N\\,dy = 0$ and test $M_y = N_x$. If "
                "equal, it is EXACT: reconstruct $F$ with $F_x = M$, $F_y = N$ "
