            # ---------------------------------------------------------- 17
            (L, "Going deeper: the solution space as a vector space", (
                "The counting rule 'order $n$ gives $n$ constants' is, for LINEAR "
                "equations, a theorem of linear algebra in disguise — and seeing it "
                "that way unifies enormous swaths of the subject.\n\n"
                "Consider a linear homogeneous equation, say "
                "$y'' + a(x)y' + b(x)y = 0$. Two facts make its solution set a VECTOR "
                "SPACE. First, if $y_1$ and $y_2$ are solutions, so is any linear "
                "combination $c_1 y_1 + c_2 y_2$ (superposition — plug it in and use "
                "linearity of differentiation). Second, the zero function is a "
                "solution. So the solutions form a subspace of the space of functions. "
                "The remarkable theorem is that this subspace has DIMENSION exactly "
                "$n$ (here 2), equal to the order. That is WHY the general solution "
                "needs exactly $n$ arbitrary constants: they are the coordinates of a "
                "vector in an $n$-dimensional space, and any BASIS "
                "$\\{y_1, \\ldots, y_n\\}$ of solutions gives the general member "
                "$c_1 y_1 + \\cdots + c_n y_n$.\n\n"
                "This raises the practical question: how do you know $y_1, \\ldots, y_n$ "
                "are a genuine basis (independent enough to span everything) rather "
                "than redundant? The answer is the WRONSKIAN, the determinant\n"
                "$$W = \\begin{vmatrix} y_1 & y_2 \\\\ y_1' & y_2' \\end{vmatrix} = "
                "y_1 y_2' - y_2 y_1',$$\n"
                "(and its higher-order analogue). If $W \\ne 0$ at some point, the "
                "solutions are linearly independent and $c_1 y_1 + c_2 y_2$ IS the "
                "general solution; the same nonzero Wronskian is what guarantees the "
                "IVP constant-system is always solvable. You will meet the Wronskian "
                "formally in Unit 2, but its role is already implicit here: it "
                "certifies that your family really is 'general.'\n\n"
                "The payoff of the vector-space viewpoint is conceptual economy. "
                "'General solution = particular + homogeneous' becomes 'a coset of the "
                "solution subspace,' exactly like the solution set of a linear system "
                "$A\\mathbf{x} = \\mathbf{b}$ being a particular solution plus the null "
                "space of $A$. The entire linear ODE theory is linear algebra performed "
                "on an infinite-dimensional space of functions, and this node's "
                "book-keeping of constants is your first glimpse of that grand "
                "unification."
            )),
