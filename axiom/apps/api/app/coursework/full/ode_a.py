                "GENERAL SOLUTION. A family with $n$ independent arbitrary constants "
                "for an $n$-th order equation; (for well-behaved equations) contains "
                "every solution. COUNTING RULE: number of constants $=$ order.\n\n"
                "PARTICULAR SOLUTION. One member of the family, obtained by fixing the "
                "constants via $n$ conditions.\n\n"
                "INITIAL-VALUE PROBLEM. All $n$ conditions at ONE point "
                "($y(x_0), y'(x_0), \\ldots$). Unique solution under Picard-Lindelof. "
                "'State now determines the future.'\n\n"
                "BOUNDARY-VALUE PROBLEM. Conditions at DIFFERENT points "
                "($y(a), y(b)$). May have NONE, ONE, or INFINITELY MANY solutions. "
                "Resonant/eigenvalue configurations give the infinite case.\n\n"
                "VERIFY BY SUBSTITUTION. Plug the candidate (constants and all) into "
                "the ODE; if it reduces to an identity for ALL constant values, it is "
                "the general solution.\n\n"
                "GEOMETRY. The family FOLIATES the plane: (first order) one non-crossing "
                "leaf through each point — the geometric form of existence-uniqueness. "
                "The constant labels the leaf.\n\n"
                "SINGULAR SOLUTIONS. Real solutions OUTSIDE the general family "
                "(Clairaut's envelope $y = -\\frac{x^2}{4}$; lost equilibria). Watch for "
                "them in nonlinear/separable problems.\n\n"
                "WORKFLOW. (1) Find the general solution, ALL constants shown. (2) Count "
                "order = constants = conditions. (3) IVP: substitute conditions at the "
                "point (differentiate for $y'$ conditions). (4) BVP: set up the "
                "constant-system and inspect for none/unique/infinite. (5) Solve for "
                "constants. (6) Verify against ODE and conditions. (7) Add any "
                "singular/equilibrium solutions.\n\n"
                "LINEAR STRUCTURE. Homogeneous solutions form an $n$-dimensional vector "
                "space; general $=$ particular $+$ homogeneous; independence certified "
                "by the Wronskian."
