            # ---------------------------------------------------------- 14
            (L, "Applications: fitting models and posing the right problem", (
                "The general/particular vocabulary is exactly how mathematical models "
                "connect to reality.\n\n"
                "MODELS PREDICT FAMILIES; DATA PICKS ONE. A physical law "
                "($F = ma$, radioactive decay, circuit dynamics) gives a differential "
                "equation whose general solution is a FAMILY of possible behaviors. The "
                "specific behavior of YOUR system is selected by measured data — the "
                "initial state, or the boundary constraints. Modeling is precisely: "
                "write the equation (get the general solution), then impose the "
                "measured conditions (get the particular solution). Without the data, "
                "the model predicts a family; with it, a definite trajectory.\n\n"
                "IVPs ARE THE LANGUAGE OF EVOLUTION. Anything evolving in time from a "
                "known present state is an initial-value problem: a projectile launched "
                "with a given position and velocity, a population with a known starting "
                "size, a circuit switched on with known initial current. The present "
                "state (all $n$ initial data) determines the future — the mathematical "
                "content of determinism.\n\n"
                "BVPs ARE THE LANGUAGE OF EQUILIBRIUM AND DESIGN. Anything pinned at "
                "its ENDS is a boundary-value problem: the steady temperature in a rod "
                "held at fixed end-temperatures, the shape of a beam clamped at both "
                "ends, the deflection of a loaded string. Here you do not evolve from a "
                "start; you satisfy constraints at the boundary. And the BVP existence "
                "subtlety has real teeth: the resonant / infinitely-many case is "
                "exactly how vibrating strings, organ pipes, and quantum systems select "
                "their DISCRETE natural frequencies and energy levels — only special "
                "parameter values admit a nonzero solution meeting the boundary "
                "conditions (Sturm-Liouville / eigenvalue problems).\n\n"
                "POSING THE RIGHT PROBLEM. A crucial modeling skill is recognizing "
                "whether your situation is an IVP or a BVP, and supplying exactly as "
                "many conditions as the order demands. Get that wrong and the model is "
                "under- or over-determined before any solving begins."
            )),
