                "hence $n$ coefficients $C_1, \\ldots, C_n$, to name a general member. "
                "The counting rule is not a rule of thumb — it is a theorem about the "
                "dimension of the solution space.\n\n"
                "VERIFYING A GENERAL SOLUTION. To confirm that a proposed family solves "
                "an equation, SUBSTITUTE it (with its constants) and check the identity "
                "holds for ALL values of the constants. For example, to verify "
                "$y = C_1 e^{2x} + C_2 e^{3x}$ solves $y'' - 5y' + 6y = 0$, compute "
                "$y' = 2C_1 e^{2x} + 3C_2 e^{3x}$ and "
