            (E, "Applied worked example: heat in a rod (IVP versus BVP)", (
                "Consider the steady-state temperature $T(x)$ in a thin insulated rod "
                "of length $L = 10$, which (with no internal heat source) obeys "
                "$T''(x) = 0$. Its general solution is $T = C_1 x + C_2$ — a straight "
                "line, two constants for order 2. We contrast supplying the data two "
                "ways.\n\n"
                "BOUNDARY-VALUE VERSION (the physical one). The ends are held at fixed "
                "temperatures: $T(0) = 100^{\\circ}$ and $T(10) = 20^{\\circ}$. From "
                "$T(0) = C_2 = 100$. From $T(10) = 10C_1 + 100 = 20$, so "
                "$C_1 = -8$. Particular solution:\n"
                "$$T(x) = 100 - 8x.$$\n"
                "CHECK: $T(0) = 100$ ✓, $T(10) = 100 - 80 = 20$ ✓, $T'' = 0$ ✓. The "
                "temperature falls LINEARLY from the hot end to the cool end — the "
                "familiar steady-state profile. This BVP has a UNIQUE solution because "
                "the two-point data pins both constants consistently.\n\n"
                "INITIAL-VALUE VERSION (for contrast). Suppose instead we knew the "
                "temperature and its GRADIENT at one end: $T(0) = 100$ and "
                "$T'(0) = 5$. From $T(0) = C_2 = 100$; differentiate $T' = C_1$, so "
                "$T'(0) = C_1 = 5$. Particular solution $T = 100 + 5x$ — a DIFFERENT "
                "line (rising), unique, determined entirely by conditions at the single "
                "point $x = 0$.\n\n"
                "SAME EQUATION, DIFFERENT PROBLEM. Both versions used two conditions to "
                "pin two constants, but the BVP fixed the two ENDS (design/equilibrium "
                "framing) while the IVP fixed the value and slope at ONE end "
                "(evolution framing). Physically the BVP is the natural model for a rod "
                "with controlled end-temperatures, and its unique linear profile is why "
                "steady heat conduction gives straight-line temperature gradients. Had "
                "we instead posed an inconsistent BVP (e.g. an equation whose family "
                "cannot meet both ends), no solution would exist — the reason engineers "
                "must check that boundary conditions are compatible with the governing "
                "equation before trusting a result."
            )),
