                "MISCONCEPTION 1: '$f'(c) = 0$ means $c$ is a max or min.' The "
                "converse-of-Fermat error, and the most common one. $x^3$ at 0 is "
                "the eternal counterexample (page 10). $f' = 0$ makes $c$ a "
                "CANDIDATE; classification requires a sign test or the "
                "second-derivative test (C114).\n\n"
                "MISCONCEPTION 2: 'Critical points are only where $f' = 0$.' The "
                "definition has TWO clauses: $f' = 0$ OR $f'$ undefined. Corners "
                "and cusps ($|x|$, $x^{2/3}$) are critical points and are often "
                "where the extremum actually lives (page 11, case c). Forgetting "
                "the second clause silently drops candidates.\n\n"
                "MISCONCEPTION 3: 'Local and absolute extrema are the same.' A "
                "local max is only the biggest NEARBY; the absolute max is the "
                "biggest ANYWHERE in the domain. A function can have a local max "
                "that is far below its absolute max (a small foothill beside a "
                "tall mountain), and endpoints can be absolute extrema without "
                "being local ones in the usual interior sense.\n\n"
                "MISCONCEPTION 4: 'The EVT tells me WHERE the max is.' It only "
                "says a max EXISTS (on a continuous function over a closed bounded "
                "interval). LOCATING it is the closed-interval method's job. "
                "Existence theorems and location methods are different tools; "
                "page 8 keeps them separate.\n\n"
                "MISCONCEPTION 5: 'The MVT lets me find the exact $c$ for "
                "applications.' In pure exercises you solve for $c$, but the MVT's "
                "real use is that SOME $c$ exists — enough to prove inequalities, "
                "bound changes, and justify the constant/increasing tests "
                "(pages 6, 9). Chasing $c$ misses the point of the theorem.\n\n"
                "MISCONCEPTION 6: 'Theorem hypotheses are formalities to skip.' "
                "Every hypothesis has a counterexample when removed (page 10). "
                "'Continuous on $[a,b]$, differentiable on $(a,b)$' is a "
                "PRECISE requirement, and problems are designed to punish "
