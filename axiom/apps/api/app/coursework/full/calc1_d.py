                "the stated domain the ABSOLUTE MAX is $39$ thousand at "
                "$t = 3$ hr; the absolute min is $-13$ at $t = 5$. Since "
                "$P'$ changes $+ \\to -$ at $t = 3$ (test $t=2$: $P'=12>0$; "
