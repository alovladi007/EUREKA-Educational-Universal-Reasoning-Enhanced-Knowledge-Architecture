                "$P'(t) = 18t - 6t^2 = 6t(3 - t)$. Critical points in $(0,5)$: "
                "$t = 0$ (boundary — it's an endpoint) and $t = 3$. Candidate "
                "list {0, 3, 5} (critical interior point plus both endpoints). "
                "Evaluate:\n"
