                "Pierre de Fermat found the 'derivative is zero at extrema' rule "
                "around 1636 — decades before Newton and Leibniz built the "
                "derivative itself — using his method of adequality, comparing "
                "$f(x)$ with $f(x + e)$ and letting the small $e$ vanish. His "
                "tangent-and-extremum work was so close to the derivative that "
                "some historians credit him as calculus's true co-founder; "
                "Fermat's theorem still bears his name as the acknowledgment.\n\n"
                "Michel Rolle (1691) proved his theorem for polynomials — "
                "ironically, while Rolle was an early CRITIC of calculus, "
                "considering it unrigorous, his algebraic result became one of "
                "its foundations. The Mean Value Theorem in its modern form was "
                "stated by Lagrange (1797) and made rigorous by Cauchy (1820s), "
                "who recognized it as the tool that converts the derivative's "
                "local definition into global theorems. The subtle piece — the "
                "Extreme Value Theorem — could not be proved until the real "
                "numbers themselves were understood: it depends on COMPLETENESS "
                "(the least-upper-bound property), formalized by Weierstrass, "
                "Dedekind, and Cantor in the 1870s. That the humble 'a "
                "continuous function on $[a,b]$ has a maximum' required "
                "constructing the real number system reveals how deep the "
                "chapter's existence claims run.\n\n"
                "The modern generalizations are sweeping. The MVT extends to "
                "the CAUCHY MEAN VALUE THEOREM (two functions, powering "
                "L'Hôpital, C115) and to TAYLOR'S THEOREM (the remainder term, "
                "C215). In higher dimensions the single $c$ is replaced by "
                "integral averages (the mean value theorem for integrals) and "
                "the extremum theory blossoms into the calculus of variations "
                "— finding not the best POINT but the best FUNCTION (the "
                "shape of a hanging chain, the path of least action in "
                "physics). Optimization itself became a discipline: convex "
                "analysis, linear and nonlinear programming, and the "
                "gradient-based training of machine-learning models are all "
                "descendants of Fermat's 1636 observation that at the top of "
                "the hill, the ground is level."
