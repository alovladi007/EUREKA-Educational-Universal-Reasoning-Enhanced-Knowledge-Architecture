                "CORRELATION. For paired data $(x_1,y_1), \\ldots, (x_n,y_n)$, the "
                "sample correlation coefficient is the average product of z-scores:\n"
                "$$r = \\frac{1}{n-1} \\sum_{i=1}^{n} "
                "\\left(\\frac{x_i - \\bar{x}}{s_x}\\right)"
                "\\left(\\frac{y_i - \\bar{y}}{s_y}\\right).$$\n"
                "It is cleaner to compute with the three CENTERED SUMS\n"
                "$$S_{xx} = \\sum (x_i - \\bar{x})^2, \\quad "
                "S_{yy} = \\sum (y_i - \\bar{y})^2, \\quad "
                "S_{xy} = \\sum (x_i - \\bar{x})(y_i - \\bar{y}),$$\n"
                "in terms of which the $n-1$ factors cancel and\n"
                "$$r = \\frac{S_{xy}}{\\sqrt{S_{xx} S_{yy}}}.$$\n"
                "The numerator $\\frac{S_{xy}}{n-1}$ is the sample COVARIANCE "
                "$s_{xy}$; correlation is covariance with the units scrubbed out: "
                "$r = \\frac{s_{xy}}{s_x s_y}$. The population version replaces "
                "sums with expectations: $\\rho = "
                "\\frac{\\operatorname{Cov}(X,Y)}{\\sigma_X \\sigma_Y}$, and $r$ "
                "estimates $\\rho$ the way $\\bar{x}$ estimates $\\mu$.\n\n"
                "THE LEAST-SQUARES LINE. Among all lines $y = a + bx$, the "
                "regression line is the one minimizing the sum of squared VERTICAL "
                "distances from the data to the line:\n"
                "$$\\text{SSE}(a,b) = \\sum_{i=1}^{n} (y_i - a - b x_i)^2.$$\n"
                "The minimizing coefficients are\n"
                "$$b = r\\,\\frac{s_y}{s_x} = \\frac{S_{xy}}{S_{xx}}, \\qquad "
                "a = \\bar{y} - b\\bar{x},$$\n"
                "and the fitted value at $x$ is $\\hat{y} = a + bx$. The "
                "$i$-th RESIDUAL is $e_i = y_i - \\hat{y}_i$ — observed minus "
                "predicted, signed, in the units of $y$.\n\n"
                "Three definitional details carry real weight. (1) VERTICAL "
                "distances, not perpendicular ones: the line is built to predict "
                "$y$ FROM $x$, so error is measured in the $y$ direction only. That "
                "asymmetry is why swapping the roles gives a different line "
                "(page 10). (2) SQUARED errors, so big misses are punished "
                "quadratically — which is why one outlier can swing the fit. "
                "(3) The formula $b = r \\frac{s_y}{s_x}$ says the slope is the "
                "correlation, re-dressed in units: $r$ is the shape of the "
                "relationship, $\\frac{s_y}{s_x}$ the exchange rate between the "
                "two measuring sticks."
