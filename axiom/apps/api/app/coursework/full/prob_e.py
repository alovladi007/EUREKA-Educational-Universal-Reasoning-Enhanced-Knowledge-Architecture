                "WHY 'REGRESSION' IS A STRANGE NAME — a historical accident. Francis "
                "Galton, studying heredity in the 1880s, saw that tall fathers had "
                "tall-but-less-tall sons and short fathers short-but-less-short "
                "ones. He published this in 1886 as 'Regression towards mediocrity "
                "in hereditary stature,' believing he had found a biological force "
                "pulling the species toward average. He had not — he had found "
                "$|r| < 1$, page 9's Example 2 exactly, time-symmetry and all, which "
                "his biological reading cannot survive. Karl Pearson formalized the "
                "coefficient (hence 'Pearson's $r$'), and a field ended up named for "
                "a misread phenomenon that had no mechanism.\n\n"
                "WHY $|r| \\le 1$: THE GEOMETRY. Center the data into vectors "
                "$\\mathbf{u} = (x_i - \\bar{x})$ and $\\mathbf{v} = "
                "(y_i - \\bar{y})$ in $\\mathbb{R}^n$. Then\n"
                "$$r = \\frac{S_{xy}}{\\sqrt{S_{xx}S_{yy}}} = "
