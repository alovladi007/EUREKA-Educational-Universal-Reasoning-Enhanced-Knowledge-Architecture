                "Think of the claim 'there is exactly one $x$ with property $P$' as a "
                "statement about a search. You are looking for objects with property $P$, and "
                "the claim says your search will find PRECISELY ONE. That single sentence "
                "makes two promises that fail in different ways, so they must be checked "
                "separately.\n\n"
                "PROMISE 1 — EXISTENCE: the search finds AT LEAST ONE. This fails if the "
                "shelves are empty — no object has property $P$ at all. Proving existence "
                "means guaranteeing the search is not fruitless.\n\n"
                "PROMISE 2 — UNIQUENESS: the search finds AT MOST ONE. This fails if there "
                "are TWO (or more) different objects with property $P$. Proving uniqueness "
                "means guaranteeing you never find a second, distinct one.\n\n"
                "The two failures are utterly independent, and all four combinations occur. "
                "'$x^2 = 4$' has TWO solutions ($\\pm 2$): existence yes, uniqueness NO. "
                "'$x^2 = -1$' over the reals has NONE: existence no (uniqueness holds "
                "vacuously — at most one, indeed at most zero). '$x^3 = 8$' has EXACTLY one "
                "real solution (2): both yes. '$0 \\cdot x = 1$' has none: existence no. "
                "Because the halves vary independently, a proof of one tells you NOTHING "
                "about the other; you must argue each.\n\n"
