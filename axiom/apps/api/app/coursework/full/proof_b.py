                "EXISTENCE says the graph reaches the target height at least once (it gets "
                "from below to above, so by continuity it must cross); UNIQUENESS says it "
                "reaches that height at most once (if the graph only ever goes UP, it cannot "
                "return to the same height twice). Existence is about the graph SPANNING the "
