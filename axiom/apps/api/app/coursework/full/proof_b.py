                "MISCONCEPTION 1: 'Strong induction is a totally different, more powerful "
                "axiom than ordinary induction.' No — they are logically EQUIVALENT (each "
                "provable from the other, and both from well-ordering). Strong induction "
                "never proves anything ordinary induction cannot; it is a convenience that "
                "matches certain problem shapes. Use it when the step naturally wants deep "
                "history, not because it is 'stronger medicine.'\n\n"
                "MISCONCEPTION 2: 'One base case is always enough.' The central strong-"
                "induction error. The number of bases must cover everything the step cannot "
                "reach: reach-back $d$ needs $d$ bases; the postage step needs FOUR. "
                "Undercounting leaves an unbridged gap (page 11).\n\n"
                "MISCONCEPTION 3: 'The strong IH lets me assume $P(k+1)$ or $P(k+2)$.' No — "
                "you may assume $P(j)$ only for $j \\le k$, i.e. values STRICTLY below the "
                "target $k + 1$. Assuming the target, or anything above it, is circular.\n\n"
                "MISCONCEPTION 4: 'Well-ordering works on any ordered set.' Only on the "
                "natural numbers (or other well-ordered sets). The reals and rationals "
                "ordered by size are NOT well-ordered — $(0,1)$ has no least element — so "
