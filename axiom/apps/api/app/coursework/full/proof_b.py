                "PROBLEM. A quantity is in equilibrium when supply $s(x) = x^3 + 2x$ equals "
                "demand $d(x) = 10 - x$, for $x \\ge 0$ (a price or level). Prove there is "
                "exactly one equilibrium.\n\n"
                "RECAST. Equilibrium means $s(x) = d(x)$, i.e. $x^3 + 2x = 10 - x$, i.e. "
                "$h(x) := x^3 + 3x - 10 = 0$. We show $h$ has exactly one zero on "
                "$[0, \\infty)$.\n\n"
                "EXISTENCE (IVT). $h$ is continuous (polynomial). $h(0) = -10 < 0$ and "
                "$h(2) = 8 + 6 - 10 = 4 > 0$. Opposite signs on $[0, 2]$, so by IVT there is "
                "an equilibrium $c \\in (0, 2)$. It EXISTS (and lies between 0 and 2, a useful "
                "bracket for a numerical solver, though we have no formula).\n\n"
                "UNIQUENESS (monotonicity). $h'(x) = 3x^2 + 3 > 0$ for all $x$, so $h$ is "
                "strictly increasing on $[0, \\infty)$ (indeed everywhere) and crosses 0 at "
                "most once. Hence the equilibrium is unique.\n\n"
                "CONCLUSION. Existence (IVT) + uniqueness (monotonicity) $\\Rightarrow$ "
                "exactly one equilibrium in $(0, 2)$. $\\blacksquare$\n\n"
                "WHY THIS MATTERS IN PRACTICE. A model with NO equilibrium is ill-posed; a "
                "model with MANY equilibria is ambiguous (which one does the system pick?). "
                "Proving exactly one — before any numerical work — certifies the model is "
                "well-posed and tells the root-finder there is a single target in a known "
