                "pure non-constructive existence; the proof guarantees a pair without "
                "identifying it.\n\n"
                "EXAMPLE 2 (existence AND uniqueness: the Division Algorithm). Prove: for "
                "integers $a$ and $b > 0$, there exist UNIQUE integers $q$ (quotient) and $r$ "
                "(remainder) with $a = bq + r$ and $0 \\le r < b$.\n\n"
                "EXISTENCE (by well-ordering — non-constructive flavor). Consider the set "
                "$S = \\{ a - bq : q \\in \\mathbb{Z},\\ a - bq \\ge 0 \\}$ of nonnegative "
                "values. $S$ is nonempty (choose $q$ very negative) and consists of "
                "nonnegative integers, so by well-ordering it has a LEAST element $r = a - "
                "bq$ for some $q$. Then $a = bq + r$ with $r \\ge 0$. If $r \\ge b$, then "
                "$a - b(q+1) = r - b \\ge 0$ would be a SMALLER member of $S$, contradicting "
                "minimality; so $r < b$. Existence of $(q, r)$ done.\n\n"
                "UNIQUENESS (suppose two, subtract). Suppose $a = bq_1 + r_1 = bq_2 + r_2$ "
                "with both remainders in $[0, b)$. Subtract: $b(q_1 - q_2) = r_2 - r_1$. The "
