                "The discipline that makes existence-uniqueness proofs reliable is "
                "ORGANIZATIONAL as much as mathematical. Three strategic habits.\n\n"
                "HABIT 1 — TWO EXPLICIT SECTIONS. Physically label them: 'Existence.' ... "
                "'Uniqueness.' ... This is not decoration; it forces you to notice if one is "
                "missing, and it prevents the commonest failure — proving existence, feeling "
                "done, and never addressing whether the object is unique (or vice versa). A "
                "reader should be able to point at each half.\n\n"
                "HABIT 2 — MATCH THE TOOL TO THE HALF, AND NEVER CROSS THEM. Existence wants "
                "a witness (construction) or an existence theorem (IVT, pigeonhole). "
                "Uniqueness wants 'suppose two, show equal' (subtraction, monotonicity, "
                "cancellation). A monotonicity argument NEVER establishes existence — a "
                "strictly increasing function might not cross zero at all (e.g. $e^x > 0$ "
                "always); it only limits how OFTEN it crosses. Conversely, IVT NEVER gives "
                "uniqueness — a function can cross zero many times. Using a uniqueness tool "
                "and thinking you have existence (or the reverse) is a structural error, not "
                "a slip.\n\n"
                "HABIT 3 — READ THE QUESTION FOR WHICH HALVES IT WANTS. Not every problem "
                "asks for both. 'Show the equation HAS a solution' asks ONLY existence. "
                "'Show a solution, if it exists, is unique' asks ONLY uniqueness (and is "
                "vacuously true if none exists). 'Exactly one' asks BOTH. 'At most one' is "
                "uniqueness alone; 'at least one' is existence alone. Answer exactly what is "
                "asked — proving uniqueness when only existence was requested wastes effort "
                "and, worse, proving only one when both were asked leaves the theorem "
                "unproved.\n\n"
                "A FINAL STRATEGIC NOTE ON VERIFICATION. When existence is CONSTRUCTIVE, the "
                "verification step is where irreversible algebra gets caught. When it is "
                "NON-CONSTRUCTIVE, there is nothing to substitute back, so the burden shifts "
                "to checking the theorem's hypotheses exactly (continuity for IVT, the "
                "counting for pigeonhole). Different existence styles, different things to "
                "double-check."
