                "THE LOGIC. 'There exists exactly one $x$ with $P(x)$,' written "
                "$\\exists! x\\, P(x)$, unpacks into a conjunction:\n"
                "$$\\underbrace{\\exists x\\, P(x)}_{\\text{EXISTENCE}} \\ \\wedge\\ "
                "\\underbrace{\\forall x\\, \\forall y\\, \\big(P(x) \\wedge P(y) "
                "\\Rightarrow x = y\\big)}_{\\text{UNIQUENESS}}.$$\n"
                "Existence is a single-witness '$\\exists$'; uniqueness is a universal claim "
                "that ANY two objects with the property coincide. This split dictates the "
                "proof: one section produces a witness, a separate section shows no two "
                "distinct witnesses can coexist. Keep them under separate headings; graders "
                "and readers look for both.\n\n"
                "EXISTENCE COMES IN TWO FLAVORS, and knowing which you are doing clarifies "
                "everything.\n\n"
                "CONSTRUCTIVE existence: you BUILD the object explicitly (or give an "
                "algorithm that produces it) and then VERIFY it has property $P$. 'The "
                "equation $2x = 6$ has a solution: namely $x = 3$, and indeed $2 \\cdot 3 = "
                "6$.' You can point at the witness. Most existence proofs you have met are "
                "constructive.\n\n"
                "NON-CONSTRUCTIVE existence: you prove an object MUST exist WITHOUT producing "
                "it. The Intermediate Value Theorem is the archetype: a continuous function "
                "that is negative at $a$ and positive at $b$ MUST be zero somewhere between — "
                "but IVT names no root, gives no formula, offers no algorithm. The pigeonhole "
                "principle is another: if $n + 1$ objects go into $n$ boxes, SOME box holds "
                "two — but pigeonhole never says which. Extremal arguments (take the largest, "
                "the smallest, the closest) are a third. These proofs guarantee a witness "
                "exists while leaving it faceless.\n\n"
                "The distinction is not pedantic. Non-constructive existence is often far "
                "EASIER (proving a root exists by IVT is trivial; finding it may be "
                "impossible in closed form) and it is exactly what many uniqueness "
