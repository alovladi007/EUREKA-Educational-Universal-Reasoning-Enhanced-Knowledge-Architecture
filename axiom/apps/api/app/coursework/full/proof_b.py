                "Most inductions follow the template mechanically. But sometimes the step "
                "STALLS — you assume $P(k)$, try to derive $P(k+1)$, and the IH is simply too "
                "weak to finish. The cure is counterintuitive and is the mark of an "
                "experienced prover: STRENGTHEN the claim. Prove a HARDER statement $P'(n)$ "
                "that implies the one you want.\n\n"
                "Why would proving MORE be EASIER? Because in an induction, a stronger $P(n)$ "
                "gives you a stronger INDUCTIVE HYPOTHESIS to work with in the step. You pay "
                "by having to prove more in the base and conclusion, but you are repaid many "
                "times over by a step that now has enough leverage. The extra assumption is "
                "exactly the fuel the step was missing.\n\n"
                "The classic illustration: prove $\\sum_{k=1}^{n} \\frac{1}{k^2} < 2$ for all "
                "$n$. Attempt the direct induction: assume $\\sum_{k=1}^{n} \\frac{1}{k^2} < "
                "2$; then $\\sum_{k=1}^{n+1} \\frac{1}{k^2} < 2 + \\frac{1}{(n+1)^2}$ — which "
                "is BIGGER than 2, not smaller. The step fails; the bound 2 gives no room. "
                "Now STRENGTHEN to $P'(n): \\sum_{k=1}^{n} \\frac{1}{k^2} \\le 2 - "
                "\\frac{1}{n}$. This is a harder claim (it says MORE — the sum stays below a "
                "moving target that is itself below 2). But now the step works: "
                "$\\sum_{k=1}^{n+1} \\frac{1}{k^2} \\le 2 - \\frac{1}{n} + "
                "\\frac{1}{(n+1)^2}$, and one checks $-\\frac1n + \\frac{1}{(n+1)^2} \\le "
