                "EXISTENCE TOOLS.\n\n"
                "• CONSTRUCTION + VERIFICATION. Solve for the witness, then CHECK it. The "
                "check is not optional: solving may use IRREVERSIBLE steps (squaring both "
                "sides, clearing denominators, taking a step that could introduce extraneous "
                "solutions), so the candidate must be substituted back into the ORIGINAL "
                "problem. A 'solution' found by squaring that fails on substitution is a "
                "phantom.\n\n"
                "• INTERMEDIATE VALUE THEOREM. For 'the equation $f(x) = k$ has a solution,' "
                "rewrite as $g(x) = f(x) - k = 0$, show $g$ is continuous, and find two "
                "points where $g$ has opposite signs. IVT then guarantees a root between "
                "them. Existence with no formula.\n\n"
                "• PIGEONHOLE. For 'some two objects share a property,' show more objects "
                "than categories; some category holds two. Existence with no construction.\n\n"
                "• EXTREMAL ARGUMENTS. Take the largest/smallest/closest object in some "
                "finite or bounded collection and argue it has the desired property; its "
                "existence comes from the collection being nonempty and bounded.\n\n"
                "UNIQUENESS TOOLS (all instances of 'suppose two, show equal').\n\n"
                "• SUBTRACTION. Assume $x_1, x_2$ both solve the problem; subtract the two "
                "equations; show the difference $x_1 - x_2$ must be 0. Cleanest for linear "
                "problems (and, generally, the difference of two solutions solves the "
                "HOMOGENEOUS problem, which you show has only the zero solution).\n\n"
                "• MONOTONICITY. If $f$ is STRICTLY increasing (or strictly decreasing), it "
                "takes each value AT MOST ONCE, so $f(x) = k$ has at most one solution. A "
                "positive derivative everywhere is the usual certificate. This is the "
                "uniqueness partner of IVT existence.\n\n"
                "• ALGEBRAIC CANCELLATION. Legal only when the thing you cancel is nonzero — "
                "the same discipline as Example 1's '$a \\ne 0$.' Dividing by a possibly-zero "
                "quantity is the classic uniqueness fallacy.\n\n"
                "STRATEGY: pick existence and uniqueness tools that fit the problem's "
                "structure, prove them in separate blocks, and never let one masquerade as "
                "the other."
