                "leaves those values unproved. Different mechanism, same lesson: identify "
                "everything the step cannot produce, and handle it explicitly.\n\n"
                "THE LIMITS OF WELL-ORDERING. Minimal-criminal arguments rely on well-"
                "ordering, which has two non-negotiable hypotheses. (1) The set of "
                "counterexamples must be NONEMPTY to have a least element — this is automatic "
                "in a proof by contradiction (you assumed a counterexample exists). (2) It "
                "must be a set of NATURAL numbers (or, generally, a well-ordered set). The "
                "principle FAILS for the reals ordered by size: the open interval $(0, 1)$ is "
                "nonempty yet has no least element; the positive rationals likewise. So you "
