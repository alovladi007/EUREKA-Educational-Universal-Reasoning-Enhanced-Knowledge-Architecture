                "Two boundary issues, both frequent sources of broken proofs.\n\n"
                "TOO FEW BASE CASES — the signature strong-induction failure. If your step "
                "reaches back $d$ steps but you supply fewer than $d$ base cases, there is a "
                "GAP: some small value is neither a base nor reachable by the step, and the "
                "cascade cannot cross it. Watch it happen with the Fibonacci bound $F_n < "
                "2^n$: suppose you gave only ONE base, $F_1 = 1 < 2$. The step for "
                "$P(2)$ needs $F_1$ AND $F_0$ — but $F_0$ was never established (and the "
                "sequence here starts at $F_1$). More visibly, the step for $P(2)$: "
                "$F_2 = F_1 + F_0$ leans on a case you skipped. With reach-back 2 you MUST "
                "verify two initial cases; one is not enough. The remedy is always the "
                "same: find the step's reach, count the unreachable initial values, and make "
                "each a base.\n\n"
