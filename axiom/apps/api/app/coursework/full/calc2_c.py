                "ratio of the smaller to the larger $\\to 0$. Logarithms lose "
                "to every positive power (even $n^{0.01}$); powers lose to "
                "every exponential (even $1.001^n$); exponentials lose to "
                "factorials; factorials lose to $n^n$ (page 7, Example 3). "
                "Within a class, compare exponents or bases: $n^3 \\gg n^2$; "
                "$3^n \\gg 2^n$ — indeed $\\frac{2^n}{3^n} = "
                "\\left(\\frac23\\right)^n \\to 0$ by the $r^n$ law.\n\n"
                "THE DECISION GUIDE:\n"
                "(1) RATIO OF POLYNOMIAL-LIKE THINGS → divide by the dominant "
                "term, read off survivors.\n"
                "(2) MIXED RACE (e.g. $\\frac{n^{10}}{2^n}$) → cite the "
                "hierarchy: exponential beats power, limit 0. Each hierarchy "
                "link is provable by repeated L'Hopital, but prove once, "
                "cite forever.\n"
                "(3) INDETERMINATE POWER ($1^\\infty$, $\\infty^0$, $0^0$) → "
                "take logarithms, then L'Hopital via the bridge theorem.\n"
                "(4) OSCILLATING FACTOR ($(-1)^n$, $\\sin n$, $\\cos n$) → "
                "if the rest $\\to 0$, squeeze with absolute values; if the "
                "rest $\\to L \\ne 0$, suspect divergence and hunt for two "
                "subsequences with different limits.\n"
                "(5) $\\infty - \\infty$ WITH ROOTS (e.g. "
                "$\\sqrt{n^2 + n} - n$) → multiply by the conjugate.\n"
                "(6) RECURSIVE DEFINITION ($a_{n+1} = g(a_n)$) → the MCT "
