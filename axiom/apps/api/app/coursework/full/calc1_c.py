                "MISCONCEPTION 1: 'The derivative is the difference quotient at "
                "some very small $h$.' No — every actual $h$, however small, gives "
                "an AVERAGE rate, an approximation. The derivative is the LIMIT, "
                "which no single evaluation reaches. This matters practically: "
                "$\\frac{f(2.001) - f(2)}{0.001}$ estimates $f'(2)$, and exam "
                "questions exploit the difference.\n\n"
                "MISCONCEPTION 2: 'The derivative is the tangent line.' The "
                "derivative $f'(a)$ is a NUMBER — the tangent line's SLOPE. The "
                "tangent line is $y = f(a) + f'(a)(x - a)$, an object built FROM "
                "the derivative and the point. Asked for a derivative, answer a "
                "number (or function); asked for a tangent line, answer an "
                "equation.\n\n"
                "MISCONCEPTION 3: 'Getting $\\frac{0}{0}$ means something went "
                "wrong.' The difference quotient at $h = 0$ is ALWAYS "
                "$\\frac{0}{0}$ — by design. That indeterminate form is the "
                "starting gun (C101, page 12) telling you to simplify; if "
                "substitution had worked, the derivative would not have needed "
                "inventing.\n\n"
                "MISCONCEPTION 4: 'Where $f$ is big, $f'$ is big.' Value and rate "
                "are independent. A hot-air balloon at high altitude can be "
                "descending ($f$ large, $f' < 0$); a function crossing zero can be "
                "changing fast. $f'$ reads STEEPNESS, not height — on a graph, "
                "never confuse the height of the curve with the tilt of the "
