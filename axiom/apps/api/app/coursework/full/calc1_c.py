                "The chain rule is the youngest-feeling of the basic rules, "
                "and historically the most casually treated. Leibniz used "
                "it implicitly from his earliest calculus manuscripts "
                "(1670s) — his differential notation made "
                "$dy = \\frac{dy}{du}\\,du$ feel automatic — and the first "
                "textbook statement appears in l'Hôpital's 1696 'Analyse "
                "des infiniment petits,' the world's first calculus "
                "textbook. For two centuries afterward, authors 'proved' "
                "it by the $\\Delta u$-cancellation argument of page 6, "
                "gap and all.\n\n"
                "The gap is real: if $g$ oscillates so that "
                "$\\Delta u = 0$ infinitely often as $\\Delta x \\to 0$ "
                "(think $g(x) = x^2 \\sin\\frac1x$ near 0 — C106's own "
                "example), the cancellation argument divides by zero. The "
                "clean repair is usually credited to Carathéodory's 1950 "
                "textbook: define the SLOPE FUNCTION "
                "$\\varphi(u) = \\frac{f(u) - f(b)}{u - b}$ for "
                "$u \\ne b$, and $\\varphi(b) = f'(b)$. Differentiability "
                "of $f$ at $b$ says exactly that $\\varphi$ is continuous "
                "at $b$, and the identity "
                "$f(u) - f(b) = \\varphi(u)(u - b)$ holds for ALL $u$ — "
                "including $u = b$, where both sides are 0. No division "
                "anywhere; compose, take limits, done. The mnemonic "
                "survives; the proof gets honest.\n\n"
                "The modern view goes further and explains WHY the rule is "
                "inevitable. A derivative is a LINEAR MAP — the best linear "
                "approximation (C105 page 17). Composing functions "
                "composes their approximations, and composing linear maps "
                "multiplies them. In one variable the 'maps' are numbers "
                "and multiplication is multiplication; in C304's several "
                "variables they are Jacobian MATRICES and the chain rule "
                "becomes matrix multiplication. Mathematicians summarize "
                "with one elegant slogan: differentiation is FUNCTORIAL — "
                "it converts composition of functions into composition of "
                "linear maps, structurally and always. The humble "
                "inner-derivative factor you drilled this chapter is the "
                "one-dimensional shadow of that theorem."
