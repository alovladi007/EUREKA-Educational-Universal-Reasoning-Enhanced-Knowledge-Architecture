                "PRODUCT RULE ('stages multiply'). One object built from independent "
                "stages with $n_1,\\dots,n_k$ options: $n_1 n_2\\cdots n_k$ outcomes. It "
                "is $|S_1\\times\\cdots\\times S_k|$. Keyword AND. Dependent stages are "
                "fine as long as each stage's COUNT is well-defined (falling products "
                "$n(n-1)\\cdots$).\n\n"
                "SUM RULE ('cases add'). Objects split into DISJOINT cases of sizes "
                "$m_1,\\dots,m_j$: total $m_1+\\cdots+m_j$. Keyword OR. DISJOINTNESS IS "
                "REQUIRED.\n\n"
                "OVERLAPPING CASES. Use inclusion-exclusion: "
                "$|A\\cup B|=|A|+|B|-|A\\cap B|$. The sum rule is the disjoint special "
                "case.\n\n"
                "COMPLEMENT RULE. good $=$ total $-$ bad. First choice for 'at least "
                "one', 'at most', 'not all', 'at least one repeat'. The complement of "
                "'at least one 6' is 'no 6'.\n\n"
                "OVERCOUNTING. If different choice-orders give the SAME (unordered) "
                "object, you overcounted; a $k$-element unordered group is counted $k!$ "
                "times, so divide by $k!$. Divide ONLY when order doesn't matter.\n\n"
                "TREE DIAGRAMS. Root-to-leaf paths $=$ outcomes; leaves $=$ count. "
                "Foolproof for small problems; reveals dependent stages and "
                "overcounting.\n\n"
                "SUBSET COUNTING. An $n$-set has $2^n$ subsets (in/out choice per "
                "element); subsets biject with length-$n$ binary strings. Includes "
                "$\\emptyset$ and the full set; 'non-empty' $\\Rightarrow 2^n-1$.\n\n"
                "BRIDGE TO DM08. Subsets of size $k$: "
                "$\\binom{n}{k}=\\frac{n(n-1)\\cdots(n-k+1)}{k!}=\\frac{n!}{k!(n-k)!}$ "
                "(falling product, then divide out order). "
                "$\\sum_{k}\\binom{n}{k}=2^n$.\n\n"
                "BOUNDARY CONVENTIONS. Empty product $=1$ (one way to do nothing), "
                "$0!=1$; a stage with 0 options makes the whole count 0.\n\n"
                "WORKFLOW. (1) Describe one object. (2) AND (product) or OR (sum)? (3) "
