                "$AB \\ne BA$, and often only one order is defined. Never reorder factors in a "
                "matrix product as if they were numbers.\n\n"
                "MISCONCEPTION 3: 'You can cancel a common factor.' From $AB = AC$ you may NOT "
                "conclude $B = C$, and from $AB = 0$ you may NOT conclude a factor is zero — "
                "unless the cancelled matrix is invertible. Matrices have zero divisors.\n\n"
                "MISCONCEPTION 4: '$(AB)^{\\top} = A^{\\top}B^{\\top}$.' Wrong order. The "
                "correct rule REVERSES the factors: "
                "$(AB)^{\\top} = B^{\\top}A^{\\top}$ (and likewise "
                "$(AB)^{-1} = B^{-1}A^{-1}$). The shapes only line up in the reversed "
                "order.\n\n"
