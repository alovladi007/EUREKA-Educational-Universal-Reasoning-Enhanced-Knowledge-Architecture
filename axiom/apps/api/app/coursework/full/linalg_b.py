                "MISCONCEPTION 1: 'Matrix multiplication is entrywise.' The single most "
                "damaging error. $AB$ is NOT the matrix of products $A_{ij}B_{ij}$; that "
                "entrywise operation exists (the Hadamard product) but is not what 'matrix "
                "multiplication' means. Every entry of $AB$ is a row-times-column DOT PRODUCT "
                "summed over the shared index.\n\n"
