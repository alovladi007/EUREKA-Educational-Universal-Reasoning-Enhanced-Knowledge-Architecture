                "STEP 3 — CLEAR BELOW. For each row beneath the pivot, subtract the right "
                "multiple of the pivot row to make its entry in the pivot column zero: if the "
                "pivot is $p$ and a lower row has entry $a$ in that column, apply "
                "$R_{\\text{low}} \\to R_{\\text{low}} - \\tfrac{a}{p}R_{\\text{pivot}}$. The "
                "number $\\tfrac{a}{p}$ is the MULTIPLIER.\n\n"
                "STEP 4 — ADVANCE. Move the current position DOWN one row and RIGHT one "
                "column, and return to Step 1. Stop when you run out of rows or columns.\n\n"
                "The result is row echelon form. Then run BACK-SUBSTITUTION: starting from the "
                "bottom pivot, solve each pivot row for its pivot variable using the values "
