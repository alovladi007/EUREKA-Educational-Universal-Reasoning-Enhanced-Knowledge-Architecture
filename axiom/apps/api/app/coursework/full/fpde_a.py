                "ERROR 1: Dropping a boundary term in integration by parts without "
                "justification. Energy methods and weak formulations live or die on the "
                "boundary integral $[u'v]$ vanishing — and it vanishes only because a "
                "specific boundary condition ($v = 0$, or $u_t = 0$ at clamped ends) "
                "kills it. Always state WHY the boundary term is zero; if the boundary "
                "condition does not kill it, it must be carried.\n\n"
                "ERROR 2: Assuming you may differentiate a series or an integral term by "
                "term. Interchanging limit, derivative, or integral requires "
                "justification (uniform convergence, dominated convergence). Many false "
                "'proofs' hinge on an unjustified swap.\n\n"
                "ERROR 3: Concluding $w \\equiv 0$ from $\\int w^2 = 0$ WITHOUT noting "
                "continuity. The step 'nonnegative integrand with zero integral implies "
                "zero integrand' needs the integrand to be continuous (or the conclusion "
                "is 'zero almost everywhere'). Harmless in practice, but state the "
                "hypothesis.\n\n"
                "ERROR 4: Misreading which data the type allows. Attempting to prove "
                "well-posedness of Laplace's equation with Cauchy data, or of backward "
                "heat, is doomed — the problem is genuinely ill-posed. Check the "
                "type-data match (page 4) before hunting for a proof that cannot "
                "exist.\n\n"
                "ERROR 5: Confusing the strong and weak maximum principles. The weak "
                "form says the max is ATTAINED on the boundary (an interior point may TIE "
                "it); the strong form says a nonconstant solution cannot attain the max "
                "in the interior at all. Uniqueness proofs use the weak form; do not "
