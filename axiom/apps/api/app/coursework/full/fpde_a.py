                "The first great tool for PROVING uniqueness and stability without ever "
                "writing a formula is the maximum principle, a rigorous form of the "
                "physical fact that heat flows from hot to cold and cannot spontaneously "
                "concentrate.\n\n"
                "THE ELLIPTIC (weak) MAXIMUM PRINCIPLE. If $u$ is harmonic "
                "($\\nabla^2 u = 0$) on a bounded domain $\\Omega$ and continuous up to "
                "the boundary, then $u$ attains both its MAXIMUM and its MINIMUM on the "
                "boundary $\\partial\\Omega$:\n"
                "$$\\max_{\\overline\\Omega} u = \\max_{\\partial\\Omega} u, \\qquad "
                "\\min_{\\overline\\Omega} u = \\min_{\\partial\\Omega} u.$$\n"
                "Intuition: at an interior maximum the graph curves down in every "
                "direction, forcing $\\nabla^2 u < 0$, contradicting $\\nabla^2 u = 0$. "
                "(The STRONG maximum principle sharpens this: a nonconstant harmonic "
                "function cannot even ATTAIN an interior max.) This is the rigorous "
                "version of the mean-value property — an interior value is the average of "
                "its surroundings, so it cannot exceed all of them.\n\n"
