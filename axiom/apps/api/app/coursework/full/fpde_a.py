                "of real interest have NO closed-form solution; the canonical three are "
                "special precisely because they do. The point of the methods is to "
                "reduce the solvable ones to series or integrals, and to reach the rest "
                "numerically. Do not expect a tidy antiderivative.\n\n"
                "MISCONCEPTION 2: 'The type of a PDE is a labelling detail.' Type "
                "dictates well-posedness. Handing an elliptic equation initial (Cauchy) "
                "data is not merely unconventional — it is ILL-POSED, with solutions "
                "that blow up under tiny perturbations (PDET's Hadamard example). "
                "Classify first, always.\n\n"
                "MISCONCEPTION 3: 'Separation of variables always works.' It works only "
                "for linear equations on domains that fit a coordinate system, with "
                "homogeneous boundary conditions. Nonlinearity, awkward geometry, or "
                "nonhomogeneous conditions break it (though the last is repairable by "
                "subtracting a steady state).\n\n"
                "MISCONCEPTION 4: 'Heat and wave behave the same because both have "
                "$u_{xx}$.' Their time derivatives differ — first order (heat) versus "
                "second order (wave) — and that one difference makes heat SMOOTH and "
                "DECAY irreversibly while waves PROPAGATE and RING reversibly. Never "
                "carry intuition from one to the other.\n\n"
                "MISCONCEPTION 5: 'The separation constant's sign is a free choice.' The "
                "boundary conditions FORCE the sign. On a Dirichlet interval only a "
                "positive constant yields eigenfunctions that vanish at both ends; "
                "picking the wrong sign gives only the trivial solution and students "
                "wrongly conclude the problem has no solution.\n\n"
                "MISCONCEPTION 6: 'Once I have the sine series I am done.' The series is "
