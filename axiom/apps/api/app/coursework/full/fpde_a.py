                "$u_t = k\\,u_{xx}$ on a space-time rectangle, the maximum is attained on "
                "the 'parabolic boundary' — the initial slice and the two spatial ends, "
                "but NOT the final time. Heat cannot create a new hot spot in the "
                "interior; temperatures are bounded by their initial and boundary "
                "values. This encodes irreversible smoothing.\n\n"
                "WHY IT PROVES THINGS. Suppose two solutions of a Dirichlet problem share "
                "the same boundary data. Their difference $w$ is harmonic with $w = 0$ on "
                "$\\partial\\Omega$, so its max and its min are both $0$ — forcing "
                "$w \\equiv 0$. Uniqueness, with no formula in sight. The same argument "
                "delivers STABILITY: if two boundary data differ by at most "
                "$\\varepsilon$, the maximum principle bounds the solutions' difference "
                "by $\\varepsilon$ everywhere. Existence needs other tools, but "
                "uniqueness and stability fall straight out of this one principle."
