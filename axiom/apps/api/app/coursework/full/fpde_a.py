                "written $H^1$ (or $W^{1,2}$): the functions in $L^2$ whose first WEAK "
                "derivatives are also in $L^2$. The subspace $H^1_0$ adds the boundary "
                "condition $u = 0$ on $\\partial\\Omega$. These are Hilbert spaces with "
                "the inner product $\\langle u, v\\rangle = \\int (uv + \\nabla u\\cdot "
                "\\nabla v)$, so all the geometry of Hilbert space — orthogonality, "
                "projection, completeness — becomes available.\n\n"
                "WHY BOTHER. In a Sobolev space, existence of a solution can be proved by "
                "abstract Hilbert-space arguments (page 14's Lax-Milgram) that no "
                "explicit formula could supply, and the results underpin the finite "
                "element method's rigor. One then studies REGULARITY: proving that a weak "
                "solution is, after the fact, actually smooth wherever the data is smooth "
