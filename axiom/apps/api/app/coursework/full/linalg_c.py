                "span of two independent vectors is a plane through the origin; the span of "
                "the standard basis is all of $\\mathbb{R}^n$.\n\n"
                "A companion fact for later: the INTERSECTION of two subspaces is always a "
                "subspace (a vector in both stays in both under add-and-scale). But the "
                "UNION of two subspaces usually is NOT — the union of the $x$-axis and the "
