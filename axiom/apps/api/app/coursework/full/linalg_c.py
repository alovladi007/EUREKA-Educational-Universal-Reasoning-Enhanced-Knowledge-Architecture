                "MISCONCEPTION 3: 'You can read a column-space basis off the reduced "
                "matrix.' Row operations CHANGE the column space, so the reduced columns "
                "are the wrong vectors. Identify WHICH columns are pivot columns, then take "
                "those columns from the ORIGINAL $A$. (The pivot POSITIONS are correct; the "
                "reduced ENTRIES are not.)\n\n"
                "MISCONCEPTION 4: 'Full rank means invertible.' Only for SQUARE matrices. A "
                "tall $3\\times 2$ matrix can have full rank ($=2$) yet is not invertible — "
                "it is not even square. Full rank means 'as independent as the shape "
                "allows'; invertibility additionally requires $m = n$.\n\n"
                "MISCONCEPTION 5: 'Multiplying matrices can increase rank.' Never: "
                "$\\text{rank}(AB) \\le \\min(\\text{rank}\\,A, \\text{rank}\\,B)$. "
                "Composition can only preserve or lose dimensions, because a composite "
                "cannot reach beyond what each factor permits. A low-rank factor is a "
                "permanent bottleneck.\n\n"
                "MISCONCEPTION 6: 'Adding rows/columns raises rank.' Appending a column "
                "that is already a combination of existing ones does NOT raise rank; only "
                "an INDEPENDENT addition can, and even then only by $1$."
