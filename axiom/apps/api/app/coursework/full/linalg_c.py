            # ---------------------------------------------------------- 13
            (P, "Common computational and procedural errors", (
                "ERROR 1: Counting zero rows as rank. Rank is the number of NONZERO rows "
                "(pivots) in echelon form. A row that reduces to all zeros contributes "
                "nothing; do not include it. A $3\\times 3$ matrix that reduces to two "
                "nonzero rows has rank $2$, not $3$.\n\n"
                "ERROR 2: Stopping elimination too early. If you quit before reaching "
                "echelon form, you may miscount pivots — a row that looks nonzero now might "
                "still reduce to zero. Carry the reduction far enough that every pivot "
                "position is unambiguous.\n\n"
                "ERROR 3: Taking column-space basis vectors from the reduced matrix. As in "
                "misconception 3: pivot POSITIONS come from the reduced form, but the basis "
                "VECTORS are the corresponding columns of the ORIGINAL $A$. Mixing these up "
                "is the most common four-subspaces mistake.\n\n"
                "ERROR 4: Confusing rank with nullity. Rank counts pivots (surviving "
                "dimensions); nullity counts free variables (collapsed dimensions). They "
                "sum to $n$, the number of COLUMNS — not to $m$. Using $m$ instead of $n$ "
                "in $n - \\text{rank}$ is a frequent slip on rectangular matrices.\n\n"
                "ERROR 5: Applying the rank bound with the wrong dimension. "
                "$\\text{rank} \\le \\min(m, n)$ uses the SMALLER of the two sizes. For a "
                "$5\\times 2$ matrix the cap is $2$, not $5$; for $2\\times 5$ it is also "
                "$2$. Always take the minimum.\n\n"
                "ERROR 6: Assuming the augmented matrix has the same rank as the "
                "coefficient matrix. For solvability you must compare "
                "$\\text{rank}(A)$ with $\\text{rank}([A \\mid \\mathbf{b}])$; a pivot "
                "appearing in the $\\mathbf{b}$ column raises the augmented rank and "
                "signals INCONSISTENCY. Reduce the augmented matrix, not just $A$, when "
                "testing existence."
            )),
