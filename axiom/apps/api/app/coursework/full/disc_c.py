                "COMPATIBILITY THEOREM. If $a \\equiv b$ and "
                "$c \\equiv d \\pmod m$, then\n"
                "$$a + c \\equiv b + d, \\qquad a - c \\equiv b - d, "
                "\\qquad ac \\equiv bd \\pmod m.$$\n"
