                "The showpiece of inclusion-exclusion sits at its "
                "logical edge: counting the elements in ALL the "
                "complements — objects avoiding every bad set at once.\n\n"
                "A DERANGEMENT is a permutation with no fixed point: "
                "$n$ people check hats, and NOBODY gets their own hat "
                "back. How many of the $n!$ permutations qualify? Call "
                "the count $D_n$.\n\n"
                "Define bad sets: $A_i$ = permutations in which person "
                "$i$ DOES get their own hat. A $k$-fold intersection "
                "$A_{i_1} \\cap \\cdots \\cap A_{i_k}$ fixes $k$ "
                "specified hats and permutes the remaining $n - k$ "
                "freely: size $(n-k)!$. There are $\\binom{n}{k}$ such "
                "intersections. Inclusion-exclusion:\n"
                "$$|A_1 \\cup \\cdots \\cup A_n| = \\sum_{k=1}^{n} "
                "(-1)^{k+1} \\binom{n}{k} (n-k)! = \\sum_{k=1}^{n} "
                "(-1)^{k+1} \\frac{n!}{k!},$$\n"
                "using $\\binom{n}{k}(n-k)! = \\frac{n!}{k!}$. Subtract "
                "from $n!$ (complement form):\n"
                "$$D_n = n! \\sum_{k=0}^{n} \\frac{(-1)^k}{k!} = n!\\left("
                "1 - 1 + \\frac{1}{2!} - \\frac{1}{3!} + \\cdots \\pm "
                "\\frac{1}{n!}\\right).$$\n\n"
                "Two remarkable consequences. FIRST, the parenthesized "
                "sum is the truncated series for $e^{-1}$, so "
                "$D_n \\approx n!/e$, and in fact $D_n$ is always the "
                "NEAREST INTEGER to $n!/e$ for $n \\ge 1$. SECOND, the "
                "proportion of derangements, $D_n/n! \\to 1/e \\approx "
                "0.3679$, stabilizes almost immediately: whether 10 hats "
