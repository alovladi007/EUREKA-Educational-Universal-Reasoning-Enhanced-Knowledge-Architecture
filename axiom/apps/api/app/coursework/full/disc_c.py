                "the second because $m \\mid a - b$ — so the sum is. "
                "∎ Sums are one line. Iterating products: "
                "$a \\equiv b \\Rightarrow a^k \\equiv b^k$ for every "
                "positive integer $k$ (induction, DM04). This theorem "
