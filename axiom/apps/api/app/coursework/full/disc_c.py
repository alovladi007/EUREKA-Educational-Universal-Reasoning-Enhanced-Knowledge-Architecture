                "$a$ and $b$ leave the same remainder on division by "
                "$m$. (Equivalence of the two versions: if "
                "$a = q_1 m + r$ and $b = q_2 m + r$ then "
                "$a - b = (q_1 - q_2)m$; conversely if "
                "$m \\mid a - b$, the division algorithm forces the "
