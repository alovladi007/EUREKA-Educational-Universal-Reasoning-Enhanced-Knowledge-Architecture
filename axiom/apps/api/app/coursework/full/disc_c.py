                "element $x$ of the union, and suppose $x$ belongs to "
                "exactly $r$ of the sets ($r \\ge 1$). Count $x$'s "
                "appearances through the formula's terms:\n\n"
                "• Among the single-set terms, $x$ is counted $r$ times — "
                "once per set containing it, i.e., $\\binom{r}{1}$.\n"
                "• Among the pairwise intersections, $x$ appears in "
                "$\\binom{r}{2}$ of them (any two of its $r$ sets), each "
                "counted with a minus sign.\n"
                "• Among $k$-fold intersections generally: "
                "$\\binom{r}{k}$ appearances, signed $(-1)^{k+1}$.\n\n"
                "Total count of $x$:\n"
                "$$\\binom{r}{1} - \\binom{r}{2} + \\binom{r}{3} - \\cdots "
                "= 1 - \\sum_{k=0}^{r} (-1)^k \\binom{r}{k} = 1 - (1 - 1)^r "
                "= 1.$$\n"
