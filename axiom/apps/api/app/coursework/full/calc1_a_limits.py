                "but the limit does not exist as a number: $\\infty$ is not a "
                "real number, it is shorthand for 'grows past every bound.' "
                "Contrast $\\frac{1}{x}$ at 0: left side $\\to -\\infty$, right "
                "side $\\to +\\infty$ — unbounded AND mismatched; here we don't "
                "even write $= \\infty$; it is simply DNE with a vertical "
                "asymptote. Vertical asymptotes get full treatment in C104.\n\n"
                "FAILURE MODE 3: OSCILLATION. The outputs never settle. The "
                "classic: $\\lim_{x \\to 0} \\sin\\frac{1}{x}$. As $x \\to 0$, "
                "$\\frac1x$ races through $100, 1000, 10000, \\ldots$, so "
