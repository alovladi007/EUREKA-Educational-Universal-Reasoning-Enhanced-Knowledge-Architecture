                "'Gets as close as you like' is intuition. Mathematics eventually "
                "demands precision, and here is the definition that Cauchy and "
                "Weierstrass distilled from 150 years of struggle:\n\n"
                "$$\\lim_{x \\to a} f(x) = L \\;\\text{ means: }\\; "
                "\\forall \\varepsilon > 0 \\;\\exists \\delta > 0 : "
                "0 < |x - a| < \\delta \\Rightarrow |f(x) - L| < \\varepsilon.$$\n\n"
                "Read it as a GAME between a skeptic and you. The skeptic hands you "
                "a tolerance $\\varepsilon$ — say, 'get $f(x)$ within $0.01$ of "
                "$L$.' You must produce a $\\delta$ — a radius around $a$ — such "
                "that EVERY $x$ within $\\delta$ of $a$ (except $a$ itself) lands "
                "$f(x)$ within $\\varepsilon$ of $L$. If you can win for EVERY "
                "$\\varepsilon$, no matter how small, the limit is $L$.\n\n"
                "Match each piece to the intuition:\n"
                "• '$\\forall \\varepsilon > 0$' = 'as close as you like' — the "
                "skeptic's tolerance can be arbitrarily tight.\n"
                "• '$\\exists \\delta > 0$' = 'close enough' — you only need SOME "
                "winning radius, and it may depend on $\\varepsilon$ (tighter "
                "tolerance, smaller radius — that's expected).\n"
                "• '$0 < |x - a|$' = 'without asking what happens at $a$' — the "
                "point itself is EXCLUDED. This little '$0 <$' is the entire "
                "limit-versus-value distinction, encoded in one symbol.\n\n"
                "A taste of it in action: prove $\\lim_{x \\to 4}(3x - 5) = 7$. "
                "Given $\\varepsilon$, we need $|3x - 5 - 7| = 3|x - 4| < "
                "\\varepsilon$, which holds whenever $|x - 4| < "
                "\\frac{\\varepsilon}{3}$. So $\\delta = \\frac{\\varepsilon}{3}$ "
                "wins the game, for every $\\varepsilon$. ∎\n\n"
                "In this course you need to UNDERSTAND this definition and use it "
                "on linear examples; wielding it on harder functions is the craft "
                "taught in PT.EPSILONDELTA and perfected in real analysis "
                "(REALAN). But even now it pays rent: every 'does the limit "
