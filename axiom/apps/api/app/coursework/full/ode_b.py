                "fact: $L[u + v] = L[u] + L[v]$ and $L[ku] = kL[u]$. That is what let "
                "$L[y - y_p] = L[y] - L[y_p] = g - g = 0$, and what let particular "
                "solutions superpose. Constant-coefficient (indeed, any linear) "
