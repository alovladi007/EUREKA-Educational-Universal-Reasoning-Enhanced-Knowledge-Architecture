                "particular' works.\n\n"
                "WHERE IT FAILS: NONLINEAR EQUATIONS. Consider "
                "$y'' + y^2 = g$ or $y'' + y\\,y' = g$ or the pendulum "
                "$\\theta'' + \\sin\\theta = 0$. The operator is NOT linear: "
                "$(u + v)^2 \\ne u^2 + v^2$, so "
                "$L[u + v] \\ne L[u] + L[v]$. For such equations you CANNOT add "
                "solutions, cannot split into homogeneous plus particular, cannot "
                "superpose forcings. Two solutions added together generally solve "
                "NOTHING. This is why nonlinear ODEs are so much harder and why "
                "there is no analogue of this chapter's clean structure, resonance, "
                "chaos, and multiple equilibria all become possible.\n\n"
                "A CONCRETE CONTRAST. For the linear $y'' + y = 0$, "
                "$\\cos x$ and $\\sin x$ are solutions and so is "
                "$3\\cos x - 2\\sin x$, any combination. For the nonlinear "
                "$y'' + y^3 = 0$, even if you find two solutions, their sum is not a "
                "solution; the amplitude changes the frequency, so you cannot mix "
                "amplitudes freely. Superposition is the exclusive privilege of linear "
                "systems.\n\n"
