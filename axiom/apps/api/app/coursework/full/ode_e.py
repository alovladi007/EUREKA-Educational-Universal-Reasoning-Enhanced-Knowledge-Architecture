                "Turn phase-portrait sketching into a fixed, repeatable routine.\n\n"
                "STEP 1 — FIND EQUILIBRIA. Factor $f$ and $g$, set each to zero, and "
                "pair the factors. Plot every equilibrium; these anchor the "
                "portrait.\n\n"
                "STEP 2 — DRAW NULLCLINES. Sketch $f = 0$ (the $x$-nullclines) and "
                "$g = 0$ (the $y$-nullclines) in two visually distinct styles. On "
                "$x$-nullclines draw tiny VERTICAL hash marks (flow is up/down); on "
                "$y$-nullclines tiny HORIZONTAL marks (flow is left/right). Their "
                "intersections should coincide with your equilibria — a built-in "
                "check.\n\n"
                "STEP 3 — MARK REGION ARROWS. The nullclines cut the plane into "
                "regions. Pick one convenient test point per region, evaluate the "
                "SIGNS of $f$ and $g$, and draw the resulting diagonal arrow "
                "(up-right / up-left / down-right / down-left). Sign is all you need, "
                "not magnitude.\n\n"
                "STEP 4 — DETERMINE CROSSING DIRECTIONS. On each nullcline segment, "
                "decide whether the vertical (or horizontal) crossing is up or down "
                "(left or right) by the sign of the OTHER component there. This orients "
                "the flow as it passes through nullclines.\n\n"
                "STEP 5 — CONNECT INTO NON-CROSSING STREAMLINES. Draw representative "
                "trajectories consistent with the region arrows and crossing "
                "directions, honoring the rule that they never cross each other or "
                "themselves. Near each equilibrium, sketch the local behavior "
                "(toward/away/around) — refined by eigenvalues in the next lessons.\n\n"
                "STEP 6 — SANITY-CHECK WITH TIME PLOTS. Pick one trajectory and "
                "confirm its $x(t)$ and $y(t)$ behavior (growing, decaying, "
                "oscillating) matches the portrait. Two representations, one truth. "
                "Follow these six steps in order and even an unfamiliar nonlinear "
                "system yields a correct qualitative picture in minutes."
