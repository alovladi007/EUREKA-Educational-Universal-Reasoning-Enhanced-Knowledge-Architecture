                "Several features sit at the boundary of the basic picture and are "
                "worth recognizing early.\n\n"
                "CLOSED ORBITS (PERIODIC SOLUTIONS). A trajectory that loops back to "
                "its start is a periodic solution: the system oscillates forever with "
                "a fixed period. In the plane, non-crossing forces a closed orbit to "
                "enclose at least one equilibrium (you cannot draw a loop of "
                "streamlines around empty flow). Predator-prey cycles and undamped "
                "oscillators live here.\n\n"
                "SEPARATRICES. Special trajectories that DIVIDE the plane into regions "
                "with different long-term fates. The paths entering or leaving a "
                "saddle point (its stable/unstable directions) act as fences: "
                "starting on one side funnels you to one equilibrium, the other side "
                "to another. Identifying separatrices tells you the 'basins of "
                "attraction' — which initial conditions lead where.\n\n"
                "LINES OF EQUILIBRIA / NON-ISOLATED CASES. If $f$ and $g$ share a "
                "common factor, an entire CURVE can be equilibria (every point on it "
                "at rest). Then the isolated-equilibrium analysis of the next lessons "
                "must be adapted; the linearization is degenerate.\n\n"
                "EQUILIBRIA AT INFINITY / UNBOUNDED FLOW. Some trajectories escape to "
                "infinity. A Poincare compactification (mapping the plane to a disk) "
                "studies behavior 'at infinity,' but for a first course it suffices to "
                "note when arrows point permanently outward.\n\n"
                "BOUNDARY-CONSTRAINED SYSTEMS. In population models the axes "
                "$x = 0$ and $y = 0$ are themselves invariant (if a species is "
