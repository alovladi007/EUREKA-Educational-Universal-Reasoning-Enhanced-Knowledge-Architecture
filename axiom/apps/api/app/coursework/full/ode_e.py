                "eigenvalues mean ROTATION (spiral or center). Stability still depends "
                "on the real part $\\tau/2$. $\\lambda = -1 \\pm 2i$ is a STABLE "
                "spiral.\n\n"
                "MISCONCEPTION 3: 'The discriminant sign decides stability.' It "
                "decides node-vs-spiral (real vs complex), NOT stability. Stability is "
                "the sign of $\\tau$; the saddle/not question is the sign of "
                "$\\Delta$. Three different quantities, three different jobs.\n\n"
                "MISCONCEPTION 4: 'A center is stable.' A linear center is only "
                "NEUTRALLY stable (orbits neither grow nor decay), and for a NONLINEAR "
                "system it is the borderline case where linearization may lie — the "
                "true equilibrium could be a stable or unstable spiral. Never report a "
                "nonlinear center from linearization alone.\n\n"
                "MISCONCEPTION 5: 'Crossing the node-spiral parabola is a "
                "bifurcation.' Crossing $\\tau^2 = 4\\Delta$ changes the SHAPE "
                "(monotone vs oscillatory approach) but not stability or topological "
                "type — the flow is qualitatively the same. The genuine bifurcations "
                "are crossing $\\Delta = 0$ (saddle-node etc.) and $\\tau = 0$ with "
                "$\\Delta > 0$ (Hopf).\n\n"
                "MISCONCEPTION 6: 'An unstable node and a saddle are the same "
                "(both unstable).' Both are unstable, but a node ($\\Delta > 0$) "
                "repels in ALL directions, while a saddle ($\\Delta < 0$) attracts "
                "along one direction and repels along another — completely different "
                "portraits and different roles (saddles create separatrices)."
