            (P, "Common misconceptions", (
                "MISCONCEPTION 1: 'Equilibria are where $x = 0$ and $y = 0$.' No — "
                "equilibria are where the DERIVATIVES vanish, $f = 0$ and $g = 0$, "
                "i.e. where $x' = 0$ AND $y' = 0$. The origin is often NOT an "
                "equilibrium, and equilibria are usually away from the axes.\n\n"
                "MISCONCEPTION 2: 'Trajectories can cross where the arrows meet.' They "
                "cannot. Uniqueness guarantees one trajectory per point; apparent "
                "crossings mean you drew the flow wrong. The only 'meeting' points are "
                "equilibria, and those are approached asymptotically, never crossed.\n\n"
                "MISCONCEPTION 3: 'The nullcline is a trajectory.' Generally false. A "
                "nullcline is where ONE component of velocity vanishes; the flow still "
                "crosses it (vertically or horizontally). A nullcline is a trajectory "
                "only in special cases (e.g. an invariant axis).\n\n"
                "MISCONCEPTION 4: 'The phase portrait shows time.' It does not — it "
                "shows the ORBITS (paths) with arrows for direction. Two trajectories "
                "can share a path shape but be traversed at different speeds; the "
                "phase plane deliberately discards timing. Use the time plots "
                "$x(t), y(t)$ when you need timing.\n\n"
                "MISCONCEPTION 5: 'A rotational arrow pattern means a center (closed "
                "orbits).' Rotation around an equilibrium signals a spiral OR a "
                "center; only eigenvalues (next lessons) decide whether the spiral "
                "winds inward/outward or the orbits close. Do not read 'center' off "
                "the arrows alone.\n\n"
                "MISCONCEPTION 6: 'More equilibria means a more complicated formula.' "
                "The number of equilibria is about the ALGEBRA of $f = g = 0$, not "
                "about solvability; nonlinear systems routinely have several "
                "equilibria and no closed-form solution at all. The whole point of "
                "the phase plane is to understand them without a formula."
            )),
