                "ERROR 1: partial-derivative slips. For $f = x(2 - x - y) = "
                "2x - x^2 - xy$, $f_x = 2 - 2x - y$ (not $2 - x - y$) and "
                "$f_y = -x$. Expand products BEFORE differentiating to avoid dropping "
                "the $-2x$ or the cross term.\n\n"
                "ERROR 2: evaluating the Jacobian at the wrong point (e.g. the origin "
                "by habit) instead of the actual equilibrium. Substitute the correct "
                "$(x_*, y_*)$ into $J(x, y)$ every time.\n\n"
                "ERROR 3: trace/determinant arithmetic. $\\Delta = ad - bc$; sign "
                "errors in $bc$ are common. For "
                "$\\begin{pmatrix} -1 & -1 \\\\ 1 & 0 \\end{pmatrix}$, "
                "$\\Delta = (-1)(0) - (-1)(1) = 0 + 1 = 1$ — the double negative is "
                "easy to botch.\n\n"
                "ERROR 4: mis-reading complex eigenvalues. From "
                "$\\lambda^2 + \\lambda + 1 = 0$, "
                "$\\lambda = \\frac{-1 \\pm \\sqrt{-3}}{2}$; the discriminant is "
                "NEGATIVE, giving complex roots with real part "
                "$-\\tfrac12$. Do not report $\\sqrt{-3}$ as real or drop the "
                "$\\frac12$.\n\n"
                "ERROR 5: forgetting to check hyperbolicity. If $\\tau = 0$ with "
                "$\\Delta > 0$, or $\\Delta = 0$, the eigenvalue verdict about "
                "STABILITY is not reliable for the nonlinear system — flag it rather "
                "than declaring 'center' or 'stable' outright.\n\n"
                "ERROR 6: confusing 'unstable node' with 'saddle.' Both are unstable, "
                "but a node has SAME-sign real eigenvalues ($\\Delta > 0, \\tau > 0$) "
                "while a saddle has opposite-sign real eigenvalues ($\\Delta < 0$). "
                "The determinant's sign distinguishes them.\n\n"
                "ERROR 7: reversing the stability inequality. Asymptotic stability "
                "needs $\\tau < 0$ (trace NEGATIVE) and $\\Delta > 0$. Writing "
