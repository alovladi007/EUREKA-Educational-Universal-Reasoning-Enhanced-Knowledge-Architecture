            # ---------------------------------------------------------- 4
            (L, "The definitions, precisely", (
                "AUTONOMOUS SYSTEM. A planar system "
                "$x' = f(x, y)$, $y' = g(x, y)$ in which $f$ and $g$ do NOT depend "
                "explicitly on $t$. Consequence: the vector field is fixed in time, so "
                "the trajectory through a point depends only on the point, not on WHEN "
                "you start — time-translations of a solution are again solutions.\n\n"
                "PHASE PLANE / STATE SPACE. The $(x, y)$ plane whose points are the "
                "possible states. A TRAJECTORY (orbit) is the image of a solution "
                "$(x(t), y(t))$ traced as $t$ varies; the arrow of increasing $t$ "
                "gives its direction.\n\n"
                "EQUILIBRIUM (critical point, fixed point, steady state). A point "
                "$(x_*, y_*)$ with\n"
                "$$f(x_*, y_*) = 0 \\quad\\text{and}\\quad g(x_*, y_*) = 0.$$\n"
                "There the velocity is zero, so the constant function "
                "$(x, y) \\equiv (x_*, y_*)$ is a solution: a state of permanent "
                "rest.\n\n"
                "NULLCLINES. The $x$-nullcline is the curve $f(x, y) = 0$; on it "
                "$x' = 0$, so trajectories cross it VERTICALLY. The $y$-nullcline is "
                "$g(x, y) = 0$; on it $y' = 0$, so trajectories cross it HORIZONTALLY. "
                "Equilibria are exactly the intersections of an $x$-nullcline with a "
                "$y$-nullcline.\n\n"
                "REGIONS AND FLOW SIGNS. The nullclines partition the plane into "
                "regions. Within each region $f$ and $g$ keep constant sign, so the "
                "flow points into one of four diagonal directions: "
                "$(f > 0, g > 0)$ up-right, $(f < 0, g > 0)$ up-left, "
                "$(f > 0, g < 0)$ down-right, $(f < 0, g < 0)$ down-left.\n\n"
                "NON-CROSSING (from uniqueness). If $f, g$ are continuously "
                "differentiable, exactly one trajectory passes through each "
                "non-equilibrium point; distinct trajectories never intersect and a "
                "single trajectory never crosses itself (except a periodic orbit, "
                "which closes up smoothly into a loop). Trajectories may APPROACH an "
                "equilibrium as $t \\to \\pm\\infty$ but never reach it in finite "
                "time."
            )),
