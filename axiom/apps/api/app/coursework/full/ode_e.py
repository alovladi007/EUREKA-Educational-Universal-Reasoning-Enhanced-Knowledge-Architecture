                "set where $f = 0$ — the current has no horizontal component there, so "
                "arrows cross it pointing straight UP or DOWN. The $y$-nullcline is "
                "where $g = 0$ — arrows cross it pointing straight LEFT or RIGHT. "
                "Where an $x$-nullcline meets a $y$-nullcline, BOTH components vanish: "
                "that intersection is an equilibrium. Between the nullclines the signs "
                "of $f$ and $g$ are constant, so the flow has a definite diagonal "
                "direction (up-right, up-left, down-right, or down-left) throughout "
                "each region.\n\n"
                "The final structural fact is that streamlines of a fluid do not "
                "cross, and neither do trajectories: through each point flows exactly "
                "one path. This single constraint — non-crossing — is astonishingly "
                "powerful. It forces trajectories to nest, to be funneled by their "
                "neighbors, and (in the plane) it is what makes closed orbits trap "
                "everything inside them. The whole sketching protocol is just: find "
                "the stagnation points, draw the nullclines, mark the regional flow "
                "directions, and connect them into non-crossing streamlines."
