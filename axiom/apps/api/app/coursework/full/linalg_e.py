                "MISCONCEPTION 1: 'Singular values are eigenvalues.' Only for "
                "symmetric positive-definite matrices. In general $\\sigma_i = "
                "\\sqrt{\\lambda_i(A^TA)}$, NOT $\\lambda_i(A)$. Singular values "
                "are always real and nonnegative; eigenvalues can be negative, "
                "complex, or (for non-square $A$) nonexistent.\n\n"
                "MISCONCEPTION 2: 'Only some matrices have an SVD.' EVERY matrix "
                "has one — square or rectangular, any rank, defective or not. That "
                "universality is the whole point; it is what diagonalization "
                "lacks.\n\n"
                "MISCONCEPTION 3: 'The singular values can be negative or in any "
                "order.' By convention they are nonnegative and sorted DECREASING: "
                "$\\sigma_1 \\ge \\sigma_2 \\ge \\cdots \\ge 0$. A 'negative "
                "singular value' is a sign error; the sign belongs in $U$ or "
                "$V$.\n\n"
                "MISCONCEPTION 4: '$U$ and $V$ are the same matrix.' Only when $A$ "
                "is symmetric positive definite. In general $V$ (right singular "
                "vectors, input frame) and $U$ (left singular vectors, output "
                "frame) are DIFFERENT orthogonal matrices — using two bases is "
                "exactly why the SVD is more flexible than $A = PDP^{-1}$.\n\n"
                "MISCONCEPTION 5: 'Low-rank approximation drops random terms.' It "
                "drops the SMALLEST singular values, and Eckart-Young proves this "
                "is OPTIMAL — no rank-$k$ matrix is closer. Keeping the largest "
                "$\\sigma_i$ retains the most 'energy.'\n\n"
                "MISCONCEPTION 6: 'The determinant measures how invertible a matrix "
                "is.' The SMALLEST SINGULAR VALUE does. A matrix can have a "
                "moderate determinant yet a tiny $\\sigma_r$ (near-singular), or a "
                "small determinant yet be perfectly well-conditioned after "
                "scaling. Condition number $\\sigma_1/\\sigma_r$, not $\\det$, is "
                "the honest measure."
