                "MISCONCEPTION 1: 'The dot product is a vector.' No — it returns a "
                "SCALAR (a single number). That is why it is also called the SCALAR "
                "product. The cross product (a different operation, only in "
                "$\\mathbb{R}^3$) returns a vector; do not confuse them.\n\n"
                "MISCONCEPTION 2: 'Orthogonal means the vectors do not intersect.' "
                "Orthogonal means PERPENDICULAR (dot product zero), a statement "
                "about DIRECTION, not about crossing. All vectors emanate from the "
                "origin, so they all 'intersect' there; orthogonality is about the "
                "$90^\\circ$ angle between their directions.\n\n"
                "MISCONCEPTION 3: 'A larger dot product means longer vectors.' The "
                "dot product mixes length AND alignment. Two short vectors pointing "
                "the same way can have a larger dot product than two long "
                "perpendicular ones (whose product is zero). Isolate alignment with "
                "$\\cos\\theta = \\frac{\\mathbf{u}\\cdot\\mathbf{v}}{|\\mathbf{u}|"
                "|\\mathbf{v}|}$, which divides out the lengths.\n\n"
                "MISCONCEPTION 4: 'Orthogonal sets need to be checked for "
                "independence.' They are AUTOMATICALLY independent (Theorem 1, "
                "provided no vector is zero). Orthogonality is a stronger condition "
                "that hands you independence for free.\n\n"
                "MISCONCEPTION 5: '$S^\\perp$ is the leftover coordinates.' It is "
                "the set of vectors perpendicular to ALL of $S$, which is a "
                "genuine subspace, not just 'the axes $S$ does not use.' Compute it "
                "as a null space, not by eyeballing.\n\n"
                "MISCONCEPTION 6: 'Dot product zero means one of the vectors is "
                "zero.' No — two NONZERO perpendicular vectors have dot product "
