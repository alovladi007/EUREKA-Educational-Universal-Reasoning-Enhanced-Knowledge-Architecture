                "A six-step workflow that survives every interval problem you will "
                "meet.\n\n"
                "STEP 1 — NAME THE PARAMETER, in words and symbols. '$\\mu$ = the "
                "mean fill weight of ALL bags produced by this machine.' Vague "
                "parameters produce vague conclusions; write the population out.\n\n"
                "STEP 2 — CHOOSE THE PROCEDURE. Quantitative data with $\\sigma$ "
                "unknown $\\Rightarrow$ one-sample $t$. Quantitative with $\\sigma$ "
                "genuinely known $\\Rightarrow$ $z$. Categorical yes/no "
                "$\\Rightarrow$ one-proportion $z$. The data type picks the tool; "
                "you do not.\n\n"
                "STEP 3 — CHECK CONDITIONS IN WRITING. Random? Independent (10% "
                "condition)? Shape (normal population, $n \\ge 30$, or a plot showing "
                "no strong skew/outliers for means; $n\\hat p \\ge 10$ and "
                "$n(1-\\hat p) \\ge 10$ for proportions)? An interval computed on "
                "unchecked conditions is a number, not evidence.\n\n"
                "STEP 4 — COMPUTE, in the recipe's order. Write $SE$ first, alone, on "
                "its own line. Then look up the critical value with the RIGHT df. "
                "Then multiply. Then add and subtract. Keeping the three parts "
                "visually separate is what makes errors findable later.\n\n"
                "STEP 5 — INTERPRET IN CONTEXT, with the parameter named. 'We are 95% "
                "confident that the mean fill weight of all bags is between 50.34 and "
                "54.26 g.' Three ingredients: confidence level, parameter in words, "
                "interval. Missing any one loses credit on every exam ever written.\n\n"
                "STEP 6 — ANSWER THE ACTUAL QUESTION. Usually there is one behind the "
                "interval: is 50 g plausible? Is the candidate above 50%? Look at "
                "whether the interval CONTAINS the value of interest. If yes, that "
                "value is plausible given the data; if no, the data argue against it. "
