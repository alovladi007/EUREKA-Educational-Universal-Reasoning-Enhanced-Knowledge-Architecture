                "EXAMPLE 1 (zero successes — the rule of three). You test $n = 50$ "
                "devices and NONE fails. $\\hat p = 0$, so the Wald interval is "
                "$0 \\pm 1.96 \\times \\sqrt{\\frac{0 \\times 1}{50}} = (0, 0)$: "
                "'the failure rate is exactly zero, guaranteed.' Nonsense — 50 trials "
                "cannot rule out a 1-in-100 defect.\n\n"
                "The honest bound comes from the binomial (PS07). If the true rate "
                "were $p$, then $P(\\text{0 failures}) = (1-p)^{50}$. Solve "
                "$(1-p)^{50} = 0.05$: $p = 1 - 0.05^{1/50} \\approx 0.0582$. So rates "
                "up to about 5.8% remain plausible. The RULE OF THREE: with zero "
                "events in $n$ trials, the 95% upper bound is $\\approx "
                "\\frac{3}{n}$ — here $\\frac{3}{50} = 0.06$, matching. 'No failures "
                "in 50 tests' is consistent with a 1-in-17 rate.\n\n"
                "EXAMPLE 2 (small counts — the plus-four fix). A sample of $n = 20$ "
                "yields 2 successes, $\\hat p = 0.10$. Conditions fail "
                "($n\\hat p = 2 < 10$), but compute the Wald interval anyway to see "
                "the damage:\n"
                "$$SE = \\sqrt{\\frac{0.1 \\times 0.9}{20}} = \\sqrt{0.0045} \\approx "
                "0.0671, \\quad ME = 1.96 \\times 0.0671 \\approx 0.1315,$$\n"
                "giving $(-0.031,\\; 0.232)$ — a NEGATIVE proportion. The PLUS-FOUR "
