                "ERROR 1: Using $z^*$ when $\\sigma$ is unknown. If the problem gives "
                "you $s$ (computed from the sample), the procedure is $t$ — at every "
                "sample size. With $n = 25$ the difference (2.064 vs 1.960) is 5% of "
                "the ME; with $n = 5$ it is 31%. Rule: $\\sigma$ from the world "
                "$\\Rightarrow z$; $s$ from the data $\\Rightarrow t$. Using $t$ "
                "always is never wrong.\n\n"
                "ERROR 2: $\\text{df} = n$ instead of $n - 1$. Small consequence at "
                "large $n$, real at small $n$, and it signals that df is being copied "
                "rather than understood.\n\n"
                "ERROR 3: $\\frac{s}{n}$ instead of $\\frac{s}{\\sqrt n}$. Still the "
                "most common arithmetic slip in the entire subject, inherited from "
                "PS13. With $n = 100$ it shrinks the ME tenfold and produces an "
                "interval so narrow it should trigger disbelief. Sanity-check: is "
                "your ME plausibly smaller than $s$ by about $\\sqrt n$?\n\n"
                "ERROR 4: Confusing ME with WIDTH. The interval "
                "$(50.34, 54.26)$ has $ME = 1.96$ and width $3.92$. 'Design an "
