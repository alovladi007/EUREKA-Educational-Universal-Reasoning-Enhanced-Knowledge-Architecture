                "need a prior probability for $H_0$ and Bayes' theorem (PS04) — the "
                "test never supplies one. Concretely: most basketball players are "
                "tall; almost no tall people are basketball players. Same two "
                "categories, opposite conditionals.\n\n"
                "MISCONCEPTION 2: 'The p-value is the probability the result was due "
                "to chance.' Misconception 1 in a costume — 'due to chance' means "
                "'$H_0$ is true,' so this is again $P(H_0 \\mid \\text{data})$. The "
                "p-value ASSUMES chance and asks how the data look under it.\n\n"
                "MISCONCEPTION 3: 'Failing to reject $H_0$ proves $H_0$.' Two "
                "verdicts exist — reject and fail to reject — and neither is 'accept.' "
