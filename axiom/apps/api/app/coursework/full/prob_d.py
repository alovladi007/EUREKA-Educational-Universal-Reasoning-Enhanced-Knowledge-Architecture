                "the ordinary formula on $\\tilde p = \\frac{2+2}{20+4} = "
                "\\frac{4}{24} \\approx 0.1667$ with $\\tilde n = 24$:\n"
                "$$\\tilde{SE} = \\sqrt{\\frac{0.1667 \\times 0.8333}{24}} \\approx "
