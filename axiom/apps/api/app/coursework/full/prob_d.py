                "but with heavier tails — so its critical values are LARGER, and the "
                "interval is wider. That extra width is the honest price of not "
                "knowing $\\sigma$.\n\n"
                "DEGREES OF FREEDOM. The $t$ family is indexed by $\\text{df} = "
                "n - 1$, inherited from $s$. Why $n - 1$? Because the $n$ deviations "
                "$x_i - \\bar x$ are not free: they must sum to zero. Fix any $n - 1$ "
                "of them and the last is forced. Only $n - 1$ independent pieces of "
                "spread information exist, and df counts them.\n\n"
                "CRITICAL VALUES (two-sided 95%), df $\\to t^*$:\n"
                "$5 \\to 2.571$; $10 \\to 2.228$; $15 \\to 2.131$; $24 \\to 2.064$; "
                "$30 \\to 2.042$; $60 \\to 2.000$; $\\infty \\to 1.960$. As df grows, "
                "$s$ pins $\\sigma$ down and $t$ melts into $z$. That is why 'use $z$ "
                "for large $n$' is a shortcut, not a different theory — and why using "
                "$t$ always is never wrong.\n\n"
                "THE TRADE-OFF TRIANGLE. $ME = t^* \\frac{s}{\\sqrt n}$ has exactly "
                "three levers. (1) CONFIDENCE up $\\Rightarrow$ $t^*$ up "
                "$\\Rightarrow$ WIDER. (2) $n$ up $\\Rightarrow$ SE down "
                "$\\Rightarrow$ NARROWER, but only as $\\frac{1}{\\sqrt n}$. "
                "(3) Population variability $s$ is a fact you don't control. You may "
                "have any two of {high confidence, narrow interval, small sample} — "
                "never all three. A 100% interval is $(-\\infty, \\infty)$: "
                "certainty, purchased with total uselessness."
