                "$R = \\infty$, interval $(-\\infty, \\infty)$, STOP. If "
                "$L(x) = \\infty$ for $x \\ne a$: $R = 0$, interval $\\{a\\}$, "
                "STOP. Otherwise $|x - a| < \\frac{1}{K} = R$.\n\n"
