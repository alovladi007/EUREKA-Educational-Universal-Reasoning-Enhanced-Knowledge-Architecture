                "MISCONCEPTION 2: 'A linear combination is just addition.' Addition is one "
                "ingredient; SCALING is the other. $\\mathbf{u} + \\mathbf{v}$ is the "
                "special combination with both weights $1$. The power comes from dialing "
                "the weights continuously, which addition alone cannot do.\n\n"
                "MISCONCEPTION 3: 'If a vector is a combination, the weights are unique.' "
                "Only when the vectors are independent. With redundant vectors (page 9, "
                "Example 2), a single target has infinitely many recipes. Membership and "
                "uniqueness are separate questions; do not assume one from the other.\n\n"
                "MISCONCEPTION 4: 'The coordinates of a vector are absolute.' Coordinates "
                "are always RELATIVE to a chosen set of vectors. The vector $(2, 3)$ has "
                "coordinates $(2, 3)$ on the standard directions but different coordinates "
                "on another basis. 'The coordinates' always means 'with respect to THIS "
                "set.'\n\n"
                "MISCONCEPTION 5: 'More vectors always let you reach more targets.' Adding a "
                "REDUNDANT vector (already a combination of the others) reaches nothing "
                "new; it only multiplies the number of recipes. What lets you reach more is "
                "adding a vector in a NEW direction — the distinction between growing the "
                "span and merely padding the list, made precise in LA.U1.N3."
