                "MISCONCEPTION 1: 'Weights have to be positive.' Nothing of the sort. "
                "$2\\mathbf{u} - 3\\mathbf{v}$ is a perfectly good linear combination with "
                "weights $2$ and $-3$. Negative and fractional weights are the norm. The "
