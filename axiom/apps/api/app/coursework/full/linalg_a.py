                "$$A(\\mathbf{x} + \\mathbf{y}) = A\\mathbf{x} + A\\mathbf{y}, \\qquad "
                "A(c\\mathbf{x}) = c\\,A\\mathbf{x}.$$\n"
                "Multiplication by $A$ RESPECTS the two vector operations: the image of a "
                "sum is the sum of images, the image of a scaled vector is the scaled "
                "image. A function with these two properties is a LINEAR TRANSFORMATION, and "
                "every matrix defines one. This is why 'linear' algebra: matrices are "
                "exactly the maps that preserve combinations. Combining the two: "
                "$A(c\\mathbf{x} + d\\mathbf{y}) = cA\\mathbf{x} + dA\\mathbf{y}$ — $A$ "
                "distributes over any linear combination.\n\n"
                "THEOREM 2 — THE SOLVABILITY CRITERION. The equation "
                "$A\\mathbf{x} = \\mathbf{b}$ has a solution IF AND ONLY IF $\\mathbf{b}$ "
                "lies in the column space $\\operatorname{Col}(A) = "
                "\\operatorname{span}\\{\\text{columns}\\}$. Reason: $A\\mathbf{x}$ ranges "
                "over exactly the combinations of the columns, so a solution exists exactly "
                "when $\\mathbf{b}$ is such a combination. Solvability is a SPAN membership "
                "question — the bridge that unifies this node with LA.U1.N2 and N3.\n\n"
                "COROLLARY — 'ALWAYS SOLVABLE.' $A\\mathbf{x} = \\mathbf{b}$ is solvable for "
                "EVERY $\\mathbf{b} \\in \\mathbb{R}^m$ iff the columns SPAN "
                "$\\mathbb{R}^m$ — iff (LA.U1.N3) the echelon form of $A$ has a pivot in "
                "every ROW. And the HOMOGENEOUS equation $A\\mathbf{x} = \\mathbf{0}$ always "
                "has at least the trivial solution $\\mathbf{x} = \\mathbf{0}$ (all dials "
                "off), by linearity. These properties turn every question about the "
                "operation into a question about pivots and span you already know how to "
                "answer."
