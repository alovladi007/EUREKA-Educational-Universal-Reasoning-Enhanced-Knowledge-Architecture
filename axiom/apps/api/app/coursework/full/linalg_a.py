                "THE SUPERPOSITION STRUCTURE. Suppose $A\\mathbf{x} = \\mathbf{b}$ has TWO "
                "distinct solutions, $\\mathbf{x}_1 \\ne \\mathbf{x}_2$. Consider their "
                "difference $\\mathbf{d} = \\mathbf{x}_2 - \\mathbf{x}_1 \\ne \\mathbf{0}$. "
                "By linearity, "
                "$A\\mathbf{d} = A\\mathbf{x}_2 - A\\mathbf{x}_1 = \\mathbf{b} - \\mathbf{b} "
                "= \\mathbf{0}$, so $\\mathbf{d}$ is a nonzero solution of the HOMOGENEOUS "
                "equation $A\\mathbf{x} = \\mathbf{0}$. Now, for EVERY scalar $t$,\n"
                "$$A(\\mathbf{x}_1 + t\\mathbf{d}) = A\\mathbf{x}_1 + t\\,A\\mathbf{d} = "
                "\\mathbf{b} + t\\,\\mathbf{0} = \\mathbf{b}.$$\n"
                "So $\\mathbf{x}_1 + t\\mathbf{d}$ is a solution for every real $t$ — an "
