                "=A^{\\mathsf{T}}\\mathbf{b}$ — the theorem behind all regression.\n\n"
                "THE SINGULAR VALUE DECOMPOSITION (LA.U9). EVERY matrix factors as "
                "$A=U\\Sigma V^{\\mathsf{T}}$ with orthogonal $U,V$ and diagonal "
