                "Linear Algebra is unified by a handful of theorems that tie solvability, "
                "structure, and spectrum together.\n\n"
                "THE INVERTIBLE MATRIX THEOREM (LA.U3-U5). For a square matrix $A$, a "
                "dozen conditions are all equivalent: $A$ is invertible; "
                "$\\det A\\ne 0$; $A\\mathbf{x}=\\mathbf{0}$ has only the trivial "
