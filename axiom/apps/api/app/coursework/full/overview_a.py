                "Calculus II is subtle precisely where 'infinite' meets 'finite.' These "
                "are the spots that trip up even strong students.\n\n"
                "SUBTLETY 1 — TERMS $\\to 0$ IS NECESSARY, NOT SUFFICIENT. If "
                "$a_n\\not\\to 0$, the series diverges (term test). But $a_n\\to 0$ does "
                "NOT guarantee convergence: the harmonic series $\\sum\\frac1n$ has terms "
                "going to 0 yet diverges. Vanishing terms are the price of admission, not "
                "a ticket to convergence.\n\n"
                "SUBTLETY 2 — ABSOLUTE VS CONDITIONAL CONVERGENCE. "
                "$\\sum\\frac{(-1)^{n+1}}{n}=\\ln 2$ converges, but "
                "$\\sum\\frac1n$ diverges — so the alternating harmonic series converges "
                "CONDITIONALLY. Conditionally convergent series can be REARRANGED to sum "
                "to any value you like (Riemann's theorem); absolutely convergent ones "
                "cannot. The distinction is not pedantry.\n\n"
                "SUBTLETY 3 — INTERVAL OF CONVERGENCE ENDPOINTS. The ratio test gives the "
                "radius, but is silent at the two endpoints, where you must test "
                "separately (often getting convergence at one end and divergence at the "
                "other). Half the work in an interval-of-convergence problem is the "
                "endpoints.\n\n"
                "SUBTLETY 4 — DISK VS SHELL, AND AXIS OF ROTATION. Choosing the wrong "
                "method or misreading the radius (distance to the axis, not the "
                "$x$-value) is the leading error in volume problems. Always draw the "
                "representative slice and label its radius against the actual axis.\n\n"
                "SUBTLETY 5 — IMPROPER INTEGRALS NEED LIMITS, NOT SHORTCUTS. "
                "$\\int_{-1}^{1}\\frac{1}{x^2}\\,dx$ is NOT $[-\\frac1x]_{-1}^1=-2$; the "
                "integrand blows up at 0, the integral is improper, and it actually "
                "DIVERGES. Blindly plugging in endpoints across a singularity produces "
