                "ERROR 1 — SIGN SLIPS IN ROW REDUCTION. A single arithmetic mistake in "
                "one row operation corrupts the entire echelon form and every conclusion "
                "drawn from it. Work slowly, write each operation, and spot-check by "
                "substituting a solution back.\n\n"
                "ERROR 2 — MULTIPLYING MATRICES IN THE WRONG ORDER OR SHAPE. "
                "$(m\\times n)(n\\times p)$ is legal and gives $m\\times p$; "
                "$(m\\times n)(p\\times q)$ with $n\\ne p$ is undefined. Check that inner "
                "dimensions match before you compute, and remember row-times-column.\n\n"
