                "MISCONCEPTION 2 — 'A sequence and a series are the same thing.' A "
                "SEQUENCE is a list $a_1,a_2,\\ldots$; a SERIES is the SUM "
                "$\\sum a_n$. The sequence $\\frac1n$ converges to 0 while the series "
                "$\\sum\\frac1n$ diverges. Ask which object the question is about.\n\n"
                "MISCONCEPTION 3 — 'The radius of convergence is the whole answer.' The "
