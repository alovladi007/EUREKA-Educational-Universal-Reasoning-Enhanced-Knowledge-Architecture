                "Probability is famously counterintuitive; its subtleties are where even "
                "careful reasoners go wrong.\n\n"
                "SUBTLETY 1 — BASE RATES DOMINATE (THE BAYES TRAP). A test's accuracy is "
                "NOT the probability you are sick given a positive result. When the "
                "condition is rare, most positives are false positives (page 5). Ignoring "
                "the prior is the single most common real-world probability error, made "
                "even by doctors.\n\n"
                "SUBTLETY 2 — INDEPENDENCE IS NOT MUTUAL EXCLUSIVITY. Disjoint events "
                "($P(A\\cap B)=0$) are actually DEPENDENT (if one happens, the other "
                "cannot). Independent events satisfy $P(A\\cap B)=P(A)P(B)$. Confusing "
                "these opposites corrupts many computations.\n\n"
                "SUBTLETY 3 — CORRELATION IS NOT CAUSATION, AND ZERO CORRELATION IS NOT "
                "INDEPENDENCE. Correlation measures only LINEAR association; variables can "
                "be strongly dependent yet have zero correlation (e.g. $Y=X^2$ on a "
                "symmetric $X$). And association never proves a causal mechanism.\n\n"
                "SUBTLETY 4 — THE CLT IS ABOUT THE AVERAGE, NOT THE INDIVIDUAL. The "
                "Central Limit Theorem says the SAMPLE MEAN becomes normal; it does not "
                "make individual data points normal. A skewed population stays skewed — "
                "only its averages bell out, and only for large enough $n$.\n\n"
                "SUBTLETY 5 — A $p$-VALUE IS NOT THE PROBABILITY THE NULL IS TRUE. It is "
                "$P(\\text{data this extreme}\\mid\\text{null true})$, not "
                "$P(\\text{null}\\mid\\text{data})$ — reversing that conditional is the "
                "Bayes trap again, and it is the most misreported statistic in science. "
                "Statistical significance is also not practical importance."
