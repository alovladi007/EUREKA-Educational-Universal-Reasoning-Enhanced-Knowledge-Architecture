                "COMPARISON AND LIMIT-COMPARISON TESTS. If $0\\le a_n\\le b_n$ and "
                "$\\sum b_n$ converges, so does $\\sum a_n$; if $\\sum a_n$ diverges, so "
                "does $\\sum b_n$. You bound an unfamiliar series by a known one.\n\n"
                "THE RATIO TEST. If $\\lim\\left|\\frac{a_{n+1}}{a_n}\\right|=L<1$, "
                "the series converges absolutely; $L>1$ diverges; $L=1$ inconclusive. This "
                "is the go-to test for factorials and powers, and it is exactly what "
                "computes the RADIUS OF CONVERGENCE of a power series.\n\n"
                "THE ALTERNATING SERIES TEST. If terms alternate in sign, decrease in "
                "magnitude, and $\\to 0$, the series converges; the truncation error is "
                "bounded by the first omitted term — a gift for approximation.\n\n"
                "TAYLOR'S THEOREM WITH REMAINDER. $f(x)=\\sum_{n=0}^{N}"
                "\\frac{f^{(n)}(a)}{n!}(x-a)^n+R_N(x)$, with $R_N$ controllable. This is "
                "the theorem that upgrades 'the series looks like $f$' to 'the series "
                "EQUALS $f$ on its interval of convergence' — the rigorous heart of the "
                "signature result."
