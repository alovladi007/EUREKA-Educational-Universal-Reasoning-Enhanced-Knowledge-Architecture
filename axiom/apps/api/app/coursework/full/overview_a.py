                "MISCONCEPTION 1 — 'If the terms go to 0, the series converges.' The "
                "harmonic series $\\sum\\frac1n$ is the eternal counterexample: terms "
                "$\\to 0$, series diverges. The term test can only prove DIVERGENCE "
