            (L, "Where the course gets subtle", (
                "Linear Algebra's difficulties are conceptual, hiding in definitions and "
                "in the gap between the finite and the geometric.\n\n"
                "SUBTLETY 1 — SPAN AND INDEPENDENCE ARE DIFFERENT QUESTIONS. Span asks "
                "'what can these vectors reach?'; independence asks 'is any of them "
                "redundant?' A set can span without being independent (too many vectors) "
                "or be independent without spanning (too few). A BASIS is the Goldilocks "
                "amount — independent AND spanning.\n\n"
                "SUBTLETY 2 — NOT EVERY MATRIX IS DIAGONALIZABLE. Diagonalization needs "
                "enough independent eigenvectors. A DEFECTIVE matrix like "
                "$\\begin{pmatrix}1&1\\\\0&1\\end{pmatrix}$ has a repeated eigenvalue but "
                "only one eigenvector direction, so it cannot be diagonalized (it needs "
                "the Jordan form instead).\n\n"
                "SUBTLETY 3 — MATRIX MULTIPLICATION IS NOT COMMUTATIVE. In general "
                "$AB\\ne BA$. Order encodes the order of transformations (rotate then "
                "reflect $\\ne$ reflect then rotate). Treating matrices like numbers is a "
                "deep source of error.\n\n"
                "SUBTLETY 4 — $A^{\\mathsf{T}}A$ VERSUS $A$. Least squares works with "
                "$A^{\\mathsf{T}}A$, a square symmetric matrix, even when $A$ is a tall "
                "rectangle with no inverse. $A^{\\mathsf{T}}A$ is invertible exactly when "
                "$A$ has independent columns — the precise condition for a unique "
                "least-squares solution.\n\n"
                "SUBTLETY 5 — ORTHOGONALITY IS A GEOMETRIC LUXURY WORTH ENGINEERING. "
                "Computations are far more stable with orthonormal bases (dot products "
                "become trivial, projections become sums). That is WHY Gram-Schmidt and "
                "the QR/SVD decompositions exist — they manufacture the orthogonality "
                "that makes everything else easy and numerically safe."
            )),
