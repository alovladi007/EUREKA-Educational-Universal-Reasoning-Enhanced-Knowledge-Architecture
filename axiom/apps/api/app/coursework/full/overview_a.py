                "THE RANK-NULLITY THEOREM (LA.U4). For an $m\\times n$ matrix, "
                "$\\text{rank}(A)+\\text{nullity}(A)=n$. Dimensions are conserved: what "
                "the transformation does not collapse (rank) plus what it sends to zero "
