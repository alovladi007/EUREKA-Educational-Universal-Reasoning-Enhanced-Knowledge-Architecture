                " - 3x^2\\rho^{-5}.$$\n"
                "Summing the three symmetric terms:\n"
                "$$\\nabla \\cdot \\vec F = 3\\rho^{-3} - 3(x^2 + y^2 + "
                "z^2)\\rho^{-5} = 3\\rho^{-3} - 3\\rho^{-3} = 0. ∎$$\n\n"
                "CLAIM 2: the outward flux through the sphere $S_a$ of "
                "radius $a$ centered at the origin is $4\\pi$, "
                "INDEPENDENT of $a$. On $S_a$, $\\vec n = \\langle x, y, z "
                "\\rangle / a$ and $\\rho = a$:\n"
                "$$\\vec F \\cdot \\vec n = \\frac{x^2 + y^2 + z^2}{a^3 "
                "\\cdot a} = \\frac{a^2}{a^4} = \\frac{1}{a^2}, \\qquad "
                "\\text{flux} = \\frac{1}{a^2} \\cdot 4\\pi a^2 = 4\\pi.$$\n"
                "The $1/\\rho^2$ decay exactly cancels the $\\rho^2$ "
                "growth of spherical area — the geometric fingerprint of "
                "inverse-square laws.\n\n"
                "CLAIM 3: EVERY closed surface $\\Sigma$ enclosing the "
                "origin carries flux $4\\pi$. Pick $a$ small enough that "
                "$S_a$ sits inside $\\Sigma$, and apply the divergence "
                "theorem to the solid BETWEEN them (origin excluded, so "
                "the hypotheses hold): the region's full boundary, "
                "outward, is $\\Sigma$ (outward) together with $S_a$ "
                "(inward), so\n"
                "$$\\text{flux}(\\Sigma) - \\text{flux}(S_a) = \\iiint 0\\,"
                "dV = 0 \\;\\Rightarrow\\; \\text{flux}(\\Sigma) = 4\\pi.$$\n"
                "Surfaces NOT enclosing the origin bound singularity-free "
                "solids: flux $0$. This is Gauss's law in its "
                "mathematical skeleton: closed-surface flux counts "
                "enclosed sources — here, the single unit source at the "
