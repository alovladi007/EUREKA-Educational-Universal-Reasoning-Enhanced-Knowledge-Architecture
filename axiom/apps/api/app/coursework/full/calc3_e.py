                "The work done by a gradient field is the CHANGE IN POTENTIAL — "
                "endpoint data only, path irrelevant.\n\n"
                "The proof is one chain rule. With $\\vec r(t)$, $a \\le t \\le "
                "b$, tracing $C$:\n"
                "$$\\frac{d}{dt} f(\\vec r(t)) = \\nabla f(\\vec r(t)) \\cdot "
                "\\vec r\\,'(t),$$\n"
