                "declaring all reals. But cancelling the $x$ assumed $x \\ne 0$, and "
                "indeed $x = 0$ makes the ORIGINAL right side $\\frac{0}{0}$ — "
                "undefined. So $x = 0$ must be EXCLUDED; the honest answer is 'all reals "
                "except $x = 0$.' The moral for linear work: do not cancel or divide by "
                "a variable expression; move terms by addition/subtraction instead, and "
                "you will never lose or invent a solution. Keep division for the final "
                "numeric coefficient only."
