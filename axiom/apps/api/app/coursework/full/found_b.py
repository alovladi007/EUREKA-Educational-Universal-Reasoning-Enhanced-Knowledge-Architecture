                "OUTERMOST layer and must come off first. The multiplication by $a$ is "
                "inner and comes off second. Undoing outermost-first keeps the "
                "coefficient's division from ever landing on the constant. Two steps, "
                "always in this order for standard form: strip the constant, then strip "
                "the coefficient. Memorize the pattern and every $ax + b = c$ becomes "
                "routine."
