                "WHAT THE EVT PROMISES — AND WHERE. On a CLOSED, BOUNDED "
                "interval, a continuous objective attains a global max "
                "and min (C113), and they occur at critical numbers or "
                "endpoints. This is why step 6 of the workflow matters "
                "so much: identifying the domain as $[0, 1200]$ converts "
                "'find the best' into a finite checklist.\n\n"
                "OPEN DOMAINS OFFER NO SUCH PROMISE. On $(0, \\infty)$, "
                "a function can decrease forever (no min attained) or "
                "grow without bound. When the domain is open, existence "
                "must be ARGUED, not assumed. The two standard "
                "arguments:\n"
                "• SINGLE-CRITICAL-POINT PRINCIPLE. If $f$ is continuous "
                "on an interval with exactly one critical number $c$, "
                "and $c$ is a local minimum, then it is the global "
                "minimum (similarly for max). Reason: if some point beat "
                "$c$, the function would have to come back down (or up) "
                "past it, manufacturing a second critical number.\n"
                "• END-BEHAVIOR ARGUMENT. Show $f(x) \\to \\infty$ at "
                "both ends of the domain (e.g. $x \\to 0^+$ and "
                "$x \\to \\infty$ for the can problem); then the "
                "interior critical point must be the global min.\n\n"
                "CONVEXITY SHORTCUT. If $f'' > 0$ on the whole domain, "
                "the function is concave up everywhere; any interior "
                "critical point is automatically the global minimum "
                "(C114's convexity payoff). Mirror statement for "
                "$f'' < 0$ and maxima — used in Example 2 of page 5.\n\n"
                "ENDPOINTS ARE REAL CANDIDATES. In applied problems, "
                "endpoint optima are not exotic: caps, budgets, and "
                "'use all of it or none of it' decisions put the best "
                "answer on the boundary, where $f' \\ne 0$ in general. "
                "Page 11 is devoted to them. The rule: whatever finishing "
                "method you use, the boundary is checked EXPLICITLY, "
