                "EXAMPLE 1 (variable coefficient, region map). Classify "
                "$x\\, u_{xx} + u_{yy} = 0$. Here $A = x, B = 0, C = 1$, so "
                "$\\Delta = 0 - 4x = -4x$. Then: $x > 0 \\Rightarrow \\Delta < 0$ ELLIPTIC; "
