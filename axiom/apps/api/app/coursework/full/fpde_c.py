                "is PARABOLIC. This is the celebrated Tricomi equation, the simplest "
                "MIXED-TYPE equation and the mathematical model for a flow that is subsonic "
                "in one region and supersonic in another (page 15).\n\n"
                "EXAMPLE 3 (characteristics in the hyperbolic region of Tricomi). For "
                "$y < 0$ write $y = -|y|$. The slopes are "
