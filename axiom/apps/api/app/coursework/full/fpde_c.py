            (L, "Techniques and strategy", (
                "A reliable classification procedure.\n\n"
                "STEP 1 — ISOLATE THE PRINCIPAL PART. Move everything to one side and "
                "identify the coefficients of $u_{xx}$, $u_{xy}$, $u_{yy}$ only. Ignore "
                "first-order and lower terms entirely. If a canonical equation is written as "
                "$u_t = k u_{xx}$, rewrite it as $k u_{xx} - u_t = 0$ so the missing "
                "$u_{tt}$ is visibly $C = 0$.\n\n"
                "STEP 2 — CHECK THE CROSS-TERM CONVENTION. Is your $B$ the full coefficient "
                "of $u_{xy}$? Then use $\\Delta = B^2 - 4AC$. If your source wrote "
                "$2B u_{xy}$, use $B^2 - AC$. Pick one and be consistent.\n\n"
                "STEP 3 — COMPUTE $\\Delta = B^2 - 4AC$ AND READ THE SIGN. Negative = "
                "elliptic, zero = parabolic, positive = hyperbolic. Attach the physics: "
                "steady/boundary, diffusion/irreversible, waves/finite-speed.\n\n"
                "STEP 4 — FOR VARIABLE COEFFICIENTS, MAP THE REGIONS. $\\Delta$ is now a "
                "function of position; solve $\\Delta = 0$ to find the parabolic dividing "
                "curves, then test a sample point in each region to label it. Report the "
                "type region by region, never globally.\n\n"
                "STEP 5 (optional but illuminating) — FIND CHARACTERISTICS. Solve "
                "$\\frac{dy}{dx} = \\frac{B \\pm \\sqrt{\\Delta}}{2A}$. Two real families "
                "confirm hyperbolic and give the coordinates that reduce the equation to "
                "$u_{\\xi\\eta} = \\ldots$; one family confirms parabolic; a complex 'pair' "
                "signals elliptic.\n\n"
                "STEP 6 — STATE THE WELL-POSED PROBLEM. Match data to type: closed-boundary "
                "data for elliptic, initial-plus-boundary for parabolic and hyperbolic "
                "(and two initial data — value AND velocity — for the second-order-in-time "
                "wave). This last step is where classification pays off in modeling."
            )),
