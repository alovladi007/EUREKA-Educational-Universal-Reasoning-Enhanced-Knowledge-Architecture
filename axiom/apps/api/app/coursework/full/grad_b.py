                "CHINESE REMAINDER THEOREM (CRT). If $m_1, \\dots, m_k$ are pairwise "
                "coprime, the simultaneous congruences $x \\equiv a_i \\pmod{m_i}$ have a "
                "unique solution modulo $M = m_1\\cdots m_k$. Structurally, arithmetic "
                "modulo $M$ SPLITS into independent arithmetic modulo each $m_i$ — a "
                "divide-and-conquer that speeds computations and underlies RSA's efficient "
                "decryption.\n\n"
                "FERMAT'S LITTLE THEOREM (FLT). If $p$ is prime and $p \\nmid a$, then "
                "$a^{p-1} \\equiv 1 \\pmod p$; equivalently $a^p \\equiv a \\pmod p$ for "
                "all $a$. So exponents can be reduced modulo $p - 1$, collapsing enormous "
                "powers.\n\n"
                "EULER'S THEOREM. The generalization to composite moduli. Euler's totient "
                "$\\varphi(n)$ counts the integers in $\\{1,\\dots,n\\}$ coprime to $n$; it "
                "is multiplicative with $\\varphi(p^k) = p^k - p^{k-1}$. Then for "
                "$\\gcd(a,n) = 1$, $a^{\\varphi(n)} \\equiv 1 \\pmod n$. This is THE theorem "
                "that makes RSA decrypt correctly.\n\n"
                "PRIMITIVE ROOTS. For a prime $p$, there exists $g$ (a primitive root) whose "
