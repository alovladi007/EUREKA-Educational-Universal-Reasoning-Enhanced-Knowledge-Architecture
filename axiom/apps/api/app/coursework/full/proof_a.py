                "WHAT IT IS. Split all possibilities into finitely many EXHAUSTIVE cases, prove "
                "the SAME conclusion $Q$ in each, conclude $Q$ holds in general. Logic: from "
                "'$C_1 \\vee \\cdots \\vee C_k$' and each '$C_i \\Rightarrow Q$', infer $Q$.\n\n"
                "NATURAL SPLITTERS. SIGN (negative / zero / positive — trichotomy). PARITY (even "
                "/ odd). RESIDUE mod $m$ (division algorithm gives $0, 1, \\ldots, m-1$). "
                "ABSOLUTE-VALUE BREAKPOINTS (cut where each inside changes sign). Absolute-value "
                "definition: $|x| = x$ if $x \\ge 0$, $-x$ if $x < 0$.\n\n"
                "TWO PILLARS. (1) EXHAUSTIVE — state and justify that the cases cover everything. "
                "(2) SAME GOAL — every case must reach the identical conclusion, or they do not "
                "combine.\n\n"
                "BOUNDARIES. Assign each boundary point to a case explicitly; prefer closed, "
                "overlapping inequalities. Overlap is harmless; a GAP is fatal.\n\n"
                "WLOG. Collapse cases only under GENUINE symmetry: name a transformation (swap "
                "variables, $x \\to -x$) mapping the omitted case onto the proven one, preserving "
                "hypotheses and goal. Otherwise prove the case.\n\n"
                "KEY EXAMPLE. $|x-1| + |x+1| \\ge 2$: three cases $x \\ge 1$ ($=2x$), "
                "$-1 \\le x \\le 1$ ($=2$, the minimum), $x \\le -1$ ($=-2x$).\n\n"
                "CAUTION. Finitely many KINDS $=$ cases; infinitely many INSTANCES (one per $n$) "
                "$=$ INDUCTION. Resolve absolute values with the sign dictated by the case. "
                "Reduce mod-$m$ constants into $\\{0, \\ldots, m-1\\}$. Use the fewest clean "
                "cases."
